                    <div class="border-radius">
                        <p>【注意】倍数：</p>
                        <p>1.现期倍数：</p>
                        <p>（1）识别：问题时间与材料一致，A 是 B 的多少倍。</p>
                        <p>（2）公式：A/B。</p>
                        <p>（3）速算：截位直除法。</p>
                        <p>2.基期倍数：</p>
                        <p>（1）识别：问题时间在材料之前，A 是 B 的多少倍。</p>
                        <p>（2）公式：A/（1+a）÷[B/（1+b）]=A/B*[（1+b）/（1+a）]。</p>
                        <p>（3）速算：①截位直除；②先算现期倍数，再判断大小。</p>
                        <p>3.倍数与增长：</p>
                        <p>（1）A 是 B 的 n 倍：n=A/B。</p>
                        <p>（2）A 比 B 增长（多）r 倍：r=A/B-1。</p>
