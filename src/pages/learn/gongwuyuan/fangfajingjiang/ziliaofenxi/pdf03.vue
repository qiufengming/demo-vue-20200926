                        <h4 class="modal-t">三、混合增长率</h4>
                        <div class="modal-c">
                            <div class="border-radius">
                                <p>【知识点】混合增长率：</p>
                                <p>1.例：已知2019年房地产利润为100亿，2018年房地产利润为80亿，求2019年房地产利润比去年增加（ ）%？</p>
                                <p>答：r=（现期- 基期）/基期=（100-80）/80。但如果材料中没有直接给出房地产利润，只给出房产和地产的数据，不可能通过公式一步解决，需要二者混合起来求增长率。</p>
                                <p>2.题型识别：部分与总体之间的增长率关系。常见概念：</p>
                                <p>（1）房产+地产=房地产。</p>
                                <p>（2）进口+出口=进出口。</p>
                                <p>（3）城镇+农村=全国。</p>
                                <p>（4）上半年+下半年=全年。</p>
                                <p>（5）硕士+博士=研究生。山东省公务员考试曾经考过研究生的常识，要作为重点，硕士是硕士研究生，博士是博士研究生，合起来是研究生的总体。</p>
                                <p>（6）1～2 月+3 月=一季度。</p>
                                <p>（7）一季度+二季度=上半年。</p>
                                <p>3.判断口诀：</p>
                                <p>（1）混合后居中（整体增长率介于部分增长率之间）。可以理解为两杯盐水混合，一杯浓度是10%，另一杯浓度是20%，混合后浓度一定介于10%～20%之间。如果多个量混合，浓度为5%、10%、28%的三杯盐水混合，混合后浓度介于5%～28%（最小值～最大值）之间。</p>
                                <p>（2）偏向基期较大的（基期指的基期量，做题中一般用现期量近似代替，答案有误差，但基本可以忽略不计）。类似于拔河的思路，哪边人多哪边就有优势：浓度为10%的盐水一杯和浓度为20%的盐水一杯混合，混合后一定居中，浓度为15%。如果20%的盐水加200杯，10%的盐水加1杯，混合后结果一定接近20%。如果10%的盐水有1000杯，20%的盐水有1杯，混合后一定接近10%。如果将一颗糖丢进游泳池中，游泳池不会变甜，因为相比之下游泳池的水特别多；如果将一滴黑色墨水滴到大海中，大海也不会发生变化，因为一滴墨水对于大海来说微不足道，起不到改变效果。</p>
                                <p>4.例：某地区2016年上半年GDP为800亿元，同比增速为8%，2016年下半年GDP为1200亿元，同比增速为12%。</p>
