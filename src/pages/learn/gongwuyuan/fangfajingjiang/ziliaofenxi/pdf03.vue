                        <h4 class="modal-t">二、基期平均数</h4>
                        <div class="modal-c">
                            <div class="border-radius">
                                <p>【知识点】基期平均数：</p>
                                <p>1.题型识别：问题时间在材料之前+平均数问法（均、每、单位）。</p>
                                <p>2.计算公式：基期平均=A/B*[（1+b）/（1+a）]</p>
                                <p>3.速算技巧：截位直除、与现期比较。</p>
                                <p>4.例：2017 年全公司收入 A 个亿，同比增长率为 a，公司有 B 口人，同比增长率为 b，问 2016 年全公司人均收入为多少？</p>
                                <p>答：给 2017 年问 2016 年，平均数=后/前，2016 年人均收入=2016 年收入/2016 年人数=A/（1+a）÷[B/（1+b）]=A/（1+a）*[（1+b）/B]=A/B*[（1+b）/（1+a）]，与基期比重的公式相同。出现“占”字是比重问题，出现平均数问法就是平均数问题。</p>
                            </div>
