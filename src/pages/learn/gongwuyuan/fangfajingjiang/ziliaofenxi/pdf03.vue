                        <p>【注意】</p>
                        <p>1.比重：</p>
                        <p>（1）关键词：占。</p>
                        <p>（2）现期比重：比重=部分/总量，部分=总量*占比，总体=部分/占比。</p>
                        <p>（3）基期比重：</p>
                        <p>&nbsp;&nbsp;- ①公式：A/B*[（1+b）/（1+a）]。</p>
                        <p>&nbsp;&nbsp;- ②速算：先计算左边 A/B，再看右边和 1 的大小关系，如果分析不出来再对右边进行精算，一般都是可以分析出来的。</p>
                        <p>（4）两期比重比较：判升降，选最小。a＞b，比重上升；a＜b，比重下降。国考中的两期比重都是选最小值，所以可以大胆预测出题人就是这样设置选项的。如果担心选最小会出错，可以用两期比重公式计算，A/B*[（a-b）/（1+a）]，计算至少需要 2 分钟，还不一定算对，不如 1 秒钟蒙出正确答案，要注重性价比。</p>
                        <p>2.平均数：</p>
                        <p>（1）关键词：平均/每/单位。“单位”指的是单位面积产量。</p>
                        <p>（2）现期平均数=后/前，每人多少钱，钱/人数；平均每班多少人，总人数/班级数。分子是 A，分母是 B。</p>
                        <p>（3）削峰填谷：第一步，找基准，尽量居中，这样多的部分和少的部分才能尽可能互补；第二步，用多的去补少的，再求平均数。</p>
                        <p>（4）基期平均：</p>
                        <p>&nbsp;&nbsp;- ①公式：A/B*[（1+b）/（1+a）]。</p>
                        <p>&nbsp;&nbsp;- ②速算：先计算左边 A/B，再看右边和 1 的大小关系。</p>
                        <p>（5）两期平均比较：a＞b，平均数上升；a＜b，平均数下降。</p>
                        <p>（6）平均数的增长率：r=（a-b）/（1+b）。a 是 A 的增长率，b 是 B 的增长率。A 永远是分子，B 永远是分母，每个班多少人，后/前，全部人数/班级数，人数就是 A，班级数就是 B。</p>
                        <p>3.倍数：</p>
                        <p>（1）关键词：倍。</p>
                        <p>（2）出现倍数，关键看时间和问法：时间上分为现期倍数和基期倍数；问法：A 是 B 的几倍，A/B；A 比 B 多几倍，A/B-1。</p>
