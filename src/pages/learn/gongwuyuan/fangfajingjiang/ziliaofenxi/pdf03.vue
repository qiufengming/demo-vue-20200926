                        <div class="modal-c">
                            <div class="border-radius">
                                <p>【知识点】现期平均（和比重没有区别）：</p>
                                <p>1.题型识别：问题时间与材料一致+平均（均、每、单位，比如人均工资，每亩的产量）。要做好和比重的区分，“占”是比重问题；“均、每、单位”是平均数问题。</p>
                                <p>2.计算公式：平均数=总数/个数=A/B。</p>
                                <p>3.计算形式：后/前（永远用后面的量/前面的量，肯定不会错）。</p>
                                <p>（1）人均收入=收入/人数。</p>
                                <p>（2）每亩的产量=产量/亩数。</p>
                                <p>（3）单位面积产量=产量/面积。</p>
                                <p>4.速算技巧：截位直除。</p>
