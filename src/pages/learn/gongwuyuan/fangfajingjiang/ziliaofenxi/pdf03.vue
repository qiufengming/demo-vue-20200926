                    </div>
                    <div class="modal-wrap">
                        <h4 class="modal-t">三、两期平均数</h4>
                        <div class="modal-c">
                            <div class="border-radius">
                                <p>【知识点】两期平均数——升降：</p>
                                <p>1.题型识别：题干中涉及两个时间+平均数问法。比如问 2018 年人均收入比 2017 年人均收入是上升还是下降？</p>
                                <p>2.公式：现期平均- 基期平均=A/B-A/B*[（1+b）/（1+a）]=A/B*[（a-b）/（1+a）]。</p>
                                <p>例：公司年底发奖金，人均奖金=钱数/人数，如果今年钱数翻了好几倍，人数只增加了一两个，那么每个人分到的钱就会变多，即 a＞b，平均数上升。如果钱数只增长了一点点，而人数招了很多，僧多粥少，那么每个人分到的钱就会变少，即 a＜b，平均数下降。</p>
                                <p>3.升降判断：方法和两期比重相同。</p>
                                <p>（1）a＞b，平均数上升。</p>
                                <p>（2）a＜b，平均数下降。</p>
                                <p>（3）a=b，平均数不变。</p>
                                <p>（4）a：分子的增长率，b：分母的增长率。</p>
                                <p>4.以下问题现期均为 2019 年，请判定题型：</p>
                                <p>（1）2019 年人均收入是多少？</p>
                                <p>答：出现“人均”，时间为现期，现期平均数问题。</p>
                                <p>（2）2018 年粮食单位面积产量是多少？</p>
                                <p>答：给 2019 年问 2018 年，单位面积产量是平均数，基期平均数问题。</p>
                                <p>（3）2019 年我的收入占全家总收入的比重比上年上升还是下降？</p>
                                <p>答：两个时间+比重，两期比重问题。</p>
                                <p>（4）2019 年人均收入比上年上升还是下降？</p>
                                <p>答：人均收入是平均数，两个时间比较，两期平均数问题。</p>
                            </div>
                            <div class="border-radius">
                                <p>【知识点】两期平均——增长率计算：</p>
                                <p>1.识别：平均数+增长+%。</p>
                                <p>例：2018 年人均工资比 2017 年人均工资增长了百分之几？如果 2018 年人均工资为 5 元，2017 年人均工资为 4 元，则增长率=（5-4）/4=25%。</p>
                                <p>2.公式：r=（a-b）/（1+b）。a 是分子的增长率，b 是分母的增长率。</p>
                                <p>推导：现期平均数为 A/B，基期平均数为 A/B*[（1+b）/（1+a）]，r=[A/B-A/B*（1+b）/（1+a）]÷[A/B*（1+b）/（1+a）]=[1-（1+b）/（1+a）]÷[（1+b）/（1+a）]=[（1+a）-（1+b）]/（1+b）=（a-b）/（1+b）。</p>
                                <p>3.做题逻辑：</p>
                                <p>（1）确定分子、分母（谁÷谁）。</p>
                                <p>（2）代入公式：r=（a-b）/（1+b）。</p>
                                <p>4.以下问题现期均为 2019 年，请判定题型：</p>
                                <p>（1）2019 年我的收入占全家总收入的比重比上年上升了几个百分点？</p>
                                <p>答：关键词是“占”，比重问题，2019 年和上年相比，为两期比重问题。a＞b，上升，a＜b，下降；判定升降之后选最小。</p>
                                <p>（2）2019 年人均收入比上年增长了百分之几？</p>
                                <p>答：关键词是“均”，平均数问题，问 2019 年比上年增长+%，为平均数的增长率，公式：（a-b）/（1+b）。</p>
                            </div>
                        </div>
                    </div>
                    <div class="border-radius">
                        <p>【注意】平均数相关：</p>
                        <p>1.现期平均数：</p>
                        <p>（1）识别：问题时间与材料一致+平均（均、每、单位）。</p>
                        <p>（2）公式：平均数=总数/个数，后面/前面</p>
