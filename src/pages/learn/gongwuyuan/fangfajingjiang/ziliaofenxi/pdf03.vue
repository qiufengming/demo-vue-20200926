                    <div class="modal-wrap">
                        <h4 class="modal-t">二、比较</h4>
                        <div class="modal-c">
                            <div class="border-radius">
                                <p>【知识点】增长量比较：</p>
                                <p>1.识别：增长最多/最少、下降最多/最少。增长率比较大小，是问增长最快/最慢。</p>
                                <p>2.题型：</p>
                                <p>（1）给现期量、基期量：增长量=现期量- 基期量。</p>
                                <p>（2）给现期量、r：</p>
                                <p>&nbsp;&nbsp;- ①大大则大。</p>
                                <p>&nbsp;&nbsp;- ②百化分计算，转化为分数比较。</p>
                            </div>
                            <div class="border-radius">
                                <p>【知识点】给现期量、r，比较增长量大小。</p>
                                <p>1.现期量大，同时│r│也大，则其增长量/减少量大。</p>
                                <p>2.一大一小：百化分计算（是 100%正确的方法），转化为分数比较：</p>
                                <p>3.例：</p>
                                <p>（1）引例 1：2017 年，小马有 200 亿，同比增长 25%，小刘有 110 亿，同比增长 10%，则 2017 年谁的增长量大？</p>
                                <p>答：小马比小刘有钱，增速还更快，则小刘永远不会追上小马，小马的增长量大于小刘的增长量，即小马现期、r 都大，则增长量大。</p>
                                <p>（2）引例 2：2017 年，小马有 200 亿，同比下降 25%，小刘有 110 亿，同比下降 10%，则 2017 年谁的减少量大？</p>
                                <p>答：小马现期大、降幅大，则小马减少量大。</p>
                                <p>（3）引例 3：2017 年，小马有 200 亿，同比增长 25%，小刘有 110 亿，同比增长 50%，则 2017 年谁的增长量大？</p>
                                <p>答：现期小马大、增长率小刘大，增长量需要计算。百化分近似估算，小马：25%=1/4，200/（4+1）=40；小刘：50%=1/2，110/3=30+，小马增长量大。</p>
                            </div>
                            <div class="border-radius">
                                <p>【注意】百化分是 100%正确的方法，看比率、速度、变化都会出现误差。</p>
                            </div>
                            <div class="border-radius">
                                <p>【注意】增长量：</p>
                                <p>1.计算：</p>
                                <p>（1）识别：增长+单位（人/元/吨）。</p>
                                <p>（2）公式：增长量=现期量- 基期量=基期量*r=现期量/（1+r）*r。</p>
                                <p>（3）速算：</p>
                                <p>&nbsp;&nbsp;- ①百分数化分数：近似转化，取中转化，倍数转化。</p>
                                <p>&nbsp;&nbsp;- ②若|r|≈1/n，增长量=现期量/（n+1）；下降量=现期量/（n-1）。</p>
                                <p>2.比较：</p>
                                <p>（1）识别：增长最多/少。</p>
                                <p>（2）速算：</p>
                                <p>&nbsp;&nbsp;- ①给出每年数据：直接两两相减，柱状图还可以用直尺标注。</p>
                                <p>&nbsp;&nbsp;- ②给出现期量和 r：两者都大则增量必然大，否则百化分计算。</p>
                            </div>
                            <div class="border-radius">
                                <p>【注意】</p>
                                <p>1.题型：增长+%→增长率；增长+单位→增长量，增长量百化分计算。</p>
                                <p>2.问增长最快/最慢，是增长率的比较；问增长最多/最少，是增长量的大小比较。</p>
                            </div>
