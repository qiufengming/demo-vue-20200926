                <h3 class="text-c section-t" @click="sectionOpenClose('s3')">第三节 基期与现期</h3>
                <div class="section-c" v-show="sectionOpen.s3">
                    <div class="border-radius">
                        <p>基本术语：</p>
                        <p>基期量与现期量</p>
                        <p>资料分析中常涉及两个量的比较，作为对比参照的时期称为基期，对应的量称为基期量；而相对于基期的时期称为现期，所对应的量称为现期量。</p>
                        <p>增长量与增长率</p>
                        <p>增长量：用来表述基期量与现期量变化的绝对量。</p>
                        <p>增长率：用来表述基期量与现期量变化的相对量。</p>
                        <p>同比与环比</p>
                        <p>同比：一般与上年同一时期相比较。</p>
                        <p>环比：与相邻的上一个时期相比较。</p>
                        <p></p>
                        <p>【知识点】基期与现期：作为对比参照的是基期，而相对于基期比较的是现期。</p>
                        <p>1.现期与基期，考试中 90%都用时间作为区分。例：2020 年我的体重 120斤，2019 年我的体重 100 斤。2020 年是现在的时间，2019 年是过去的时间，所以 2020 年为现期，2019 年为基期。要有时间轴概念，如果给 2019 年，求 2017、2012 年，向 2019 年左侧推就是基期；现期是未来的、没有发生的事情，给 2019年，求 2020 年，向 2019 年右侧推就是求现期。</p>
                        <p>2.2020 年比 2019 年增长了 20 斤（增长量），增长的具体值就是增长量。增长量=现在-过去=现期- 基期=120-100=20 斤。</p>
                        <p>3.2020 年比 2019 年增长 20%（增长率）。增长率（rate）就是增长的比例，用 r 表示，增长率=增长量/基期量=（现期量- 基期量）/基期量=（120-100）/100=20%。增长量和增长率问题下节课具体讲。</p>
                        <p>4.有一种特殊表述：比如马云比我有钱，作为对比参照的是基期，“我”就是基期，“马云”就是现期；再比如我比马云帅，马云作为对比参照，所以“马云”就是基期，“我”就是现期。对比型题目总结：“比”字后面是基期，出现“A比 B……”，B 是基期，A 是现期。</p>
                    </div>
                    <div class="modal-wrap">
                        <h4 class="modal-t">一、基期量</h4>
                        <div class="modal-c">
                            <div class="border-radius">
                                <p>【知识点】基期量：</p>
                                <p>1.题型识别：给现在，求以前某时期的值。做题第一步先确定时间，圈出时间，问题时间在材料之前，比如材料给 2019 年，问 2018 年或者 2017年，求以前的时间（通过时间去区分），就是基期。</p>
                                <p>2.计算公式：基期量=现期-增长量（最简单）。比如现在体重 120 斤，比过去增长了 20 斤，过去就是 120-20=100 斤。</p>
                            </div>
                            <div class="border-radius">
                                <p>【注意】资料分析中加减做差的问题，选项精度和材料精度保持一致时，可以用尾数法解题。</p>
                            </div>
                            <div class="border-radius">
                                <p>【知识点】基期量：</p>
                                <p>1.题型识别：给现期，求前面某个时期的值。</p>
                                <p>2.列式：</p>
                                <p>（1）给现期量和增长量：基期量=现期量-增长量（尾数法、估算）。</p>
                                <p>（2）给现期量和增长率：基期量=现期量/（1+r）（今天的重点）。</p>
                                <p>3.速算：r 为 rate，代表增长率。</p>
                                <p>（1）|r|＞5%，截位直除。</p>
                                <p>（2）|r|≤5%（选项差距小），化除为乘（后面会讲到）。</p>
                            </div>
                            <div class="border-radius">
                                <p>【知识点】同比与环比：</p>
                                <p>1.同比：与上年同期相比。比如 2019 年的 7 月同比对应的是 2018 年 7 月；2019 年一季度同比对应的是 2018 年一季度。</p>
                                <p>2.环比：与紧紧相邻的上一统计周期相比（月不环比、季度环比）。环比类似于“铁链子”，环环相扣。比如 2019 年 7 月的环比，7 月的“上一环”是 6 月，环比对应的是 2019 年 6 月；2019 年一季度的环比，一季度的“上一环”是四季度，环比对应的是 2018 年四季度。</p>
                            </div>
                            <div class="border-radius">
                                <p>【注意】一旦增长率比较小的时候，除起来很麻烦，可以用化除为乘计算。</p>
                            </div>
                            <div class="border-radius">
                                <p>【知识点】化除为乘：</p>
                                <p>1.应用：求基期，选项差距小，|r|≤5%。</p>
                                <p>2.方法：A/（1-r）≈A+A*r；A/（1+r）≈A-A*r。A 为现期量。</p>
                                <p>3.推导：分子分母同时乘以（1+r），分数值不变，A/（1-r）=A*（1+r）/[（1+r）*（1-r）]，根据平方差公式（a+b）*（a-b）=a²-b²，可以得到：（1+r）*（1-r）=1-r²，原式=（A+A*r）/（1-r²）。当|r|≤5%，r²=5%²=0.05²=0.0025，1-0.0025≈1，因此（A+A*r）/（1-r²）≈（A+A*r）/1=A+A*r。</p>
                                <p>同理：A/（1+r）=A*（1-r）/[（1+r）*（1-r）]=A*（1-r）/（1-r²）≈A-A*r。</p>
                            </div>
                            <div class="border-radius">
                                <p>【知识点】基期和差：</p>
                                <p>1.以坑治坑：先观察现期坑，排除；再看大小关系，选择。</p>
                                <p>2.截位直除：60%以上的基期和差类问题，都可以用以坑治坑的思路做，但也不是 100%能用，如果以坑治坑，不好用了，再去截位直除。</p>
                            </div>
                        </div>
                    </div>
                    <div class="modal-wrap">
                        <h4 class="modal-t">二、现期量</h4>
                        <div class="modal-c">
                            <div class="border-radius">
                                <p>【知识点】现期量：</p>
                                <p>1.题型识别：给现在，求后面某时间的值。考试 80%都是基期计算，剩下 20%才是极个别的现期计算，所有的现期问题是基期的翻版。比如 2020 年体重是 120斤，按照同样的增长标准，问 10 年之后的体重。</p>
                                <p>2.计算公式：</p>
                                <p>（1）现期量=基期量+增长量。</p>
                                <p>（2）现期量=基期量*（1+增长率）。</p>
                                <p>3.举例</p>
                                <p>（1）示例 1：2016 年产值为 100 万，2017 年比 2016 年多 50 万，则 2017年产值为多少？</p>
                                <p>&nbsp;&nbsp;- 答：给 2016 年，问未来的 2017 年，即时间轴右侧的 2017 年，求现期。现期=基期+增长量=100+50=150。</p>
                                <p>（2）示例 2：2016 年产值为 100 万，2017 年比 2016 年增长了 50%，则 2017年产值为多少？</p>
                                <p>&nbsp;&nbsp;- 答：给了基期和增长率，2017 年=2016 年*（1+r）=100*（1+50%）。</p>
                            </div>
                        </div>
                    </div>
                    <div class="border-radius">
                        <p>【注意】基期与现期：</p>
                        <p>1.基期量：</p>
                        <p>（1）识别：求前面某个时期的量。</p>
                        <p>（2）公式：基期量=现期量-增长量，基期量=现期量/（1+r）。</p>
                        <p>（3）速算：r 大截位直除，r 小化除为乘。</p>
                        <p>（4）基期和差：先用现期和正负排除再计算。</p>
                        <p>2.现期量：</p>
                        <p>（1）识别：求后面某个时期的量。</p>
                        <p>（2）公式：现期量=基期量+增长量；现期量=基期量*（1+r）。</p>
                        <p>（3）速算：截位计算，特殊数字。</p>
                        <p>3.注意：今天的重点是计算（截位直除）、比较（分数比较）和基期与现期。</p>
                    </div>
