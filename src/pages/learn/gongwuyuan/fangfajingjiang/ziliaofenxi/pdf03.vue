                    <div class="modal-wrap">
                        <h4 class="modal-t">一、计算</h4>
                        <div class="modal-c">
                            <div class="border-radius">
                                <p>【知识点】增长量：</p>
                                <p>1.识别：增长+具体单位。</p>
                                <p>（1）增长量有具体单位描述，比如今年增加 200 人、今年增加 200 元。</p>
                                <p>（2）区分：增长+%→增长率；增长+具体单位→增长量。</p>
                                <p>2.例：2017 年比 2016 年增加了多少亿元。</p>
                                <p>3.计算公式：增长量=现期- 基期=基期*r=现期/（1+r）*r。</p>
                                <p>4.比如现在是 120 斤，过去是 100 斤，增长了 120-100=20 斤。</p>
                            </div>
                            <div class="border-radius">
                                <p>【知识点】年均增长量：</p>
                                <p>1.识别：年均+增长+单位。</p>
                                <p>2.年均增长量=（现期量- 基期量）/年份差。比如老师大学四年共增长了 20 斤，则平均每年增长了 20/4=5 斤。</p>
                                <p>3.年均增长类问题年份差的选择：</p>
                                <p>（1）一般情况（除江苏外）：2011 年～2015 年：年份差 n=2015-2011=4。基期：2011 年；现期：2015 年。除了江苏省考，其他所有地方方法都一样（包括国考）。</p>
                                <p>（2）江苏省考：2011 年～2015 年：年份差为 5（基期往前推一年）；基期：2010 年；现期：2015 年。江苏出题人的标准不同，只要不是参加江苏省考的同学，不需要记，如果题库刷题遇到，可以跳过，或者按照基期往前推一年计算。</p>
                                <p>（3）五年规划（全国都一样）：“十二五”期间（2011～2015 年）：年份差为 5（基期往前推一年）；基期：2010 年；现期：2015 年。五年规划，所有地区都相同，不论什么考试，都是一样的。比如“十三五”期间（2016～2020 年），现期是 2020 年，基期前推一年是 2015 年。</p>
                            </div>
                            <div class="border-radius">
                                <p>【注意】</p>
                                <p>1.本题如果一个一个计算，中间会约掉，这种题目，找到现期和基期代公式即可。</p>
                                <p>2.年均增加值，通常增加值是专有名词，可以理解为 GDP，和增长量无关，但是本题只能理解为增长量，否则没有答案。</p>
                                <p>3.如果依次计算 2012 年-2011 年、2013 年-2012 年、2014 年-2013 年、2015年-2014 年，四年的增长量加和，中间会约掉，最后只有 2011 年和 2015 年，因此这种题目找到现期和基期计算即可。</p>
                            </div>
                            <div class="border-radius">
                                <p>【注意】</p>
                                <p>1.所有的五年规划，年份差都是 5。</p>
                                <p>2.给“十一五”，基期需要前推，如果是正常的时间段，不需要前推，和题目的数据无关。</p>
                                <p></p>
                                <p>【知识点】已知现期、增长率，求增长量，方法是百化分。</p>
                                <p>1.公式：增长量=现期/（1+r）*r。现期/（1+r）=基期，基期*r=增长量。公式不需要记，考试基本不会用。</p>
                                <p>2.2019 年总收入是 10 万元，同比增长 33.3%。求：2019 年与 2018 年相比总收入增长了多少万元？分析：</p>
                                <p>（1）求增长量，如果用增长量=现期/（1+r）*r 计算，增长量=10/（1+33.3%）*33.3%，计算很麻烦。</p>
                                <p>（2）33.3%≈1/3，原式≈10÷（1+1/3）*1/3=10/（1+3）=2.5</p>
                                <p>（3）增长量计算的核心是如何巧妙的把百分数转化为分数的形式——百化分。</p>
                                <p>3.结论：</p>
                                <p>（1）增长率百化分，|r|=1/n。</p>
                                <p>（2）r＞0 时，增长量=现期/（n+1）；r＜0 时，减少量=现期/（n-1）。n 为百化分的分母。</p>
                                <p>（3）推导：</p>
                                <p>&nbsp;&nbsp;- ①r＞0 时：r 是百分数，一定可以转化为 1/n 的形式，增长量=现期/（1+r）*r=现期÷（1+1/n）*（1/n）=现期/（n+1）。</p>
                                <p>&nbsp;&nbsp;- ②r＜0 时，r=-1/n，增长量=现期÷（1-1/n）*（-1/n）=-现期÷（1-1/n）*1/n→增长量=-现期/（n-1），可以表述为减少量=现期/（n-1），“减少”相当于“-”。</p>
                                <p>4.例：2019 年总收入是 10 万元，同比下降 33.3%。求：2019 年与 2018 年相比总收入减少了多少万元？</p>
                                <p>答：“下降 33.3%”，r＜0，转化为分数是-1/3，减少量=现期/（n-1）=10/（3-1）=5。</p>
                            </div>
                            <div class="border-radius">
                                <p>【知识点】百化分：</p>
                                <p>1.百化分表格：</p>
                                <p>（1）1/2=50%，1/4=25%，1/8=12.5%，1/16=6.25。</p>
                                <p>（2）1/3≈33.3%，1/6≈16.7%，1/12≈8.3%。</p>
                                <p>（3）1/5=20%，1/10=10%，1/20=5%。</p>
                                <p>（4）1/7≈14.3%，1/14≈7.1%。</p>
                                <p>（5）1/9≈11.1%，1/11≈9.1%。</p>
                                <p>（6）1/13≈7.7%，1/15≈6.7%。</p>
                                <p>（7）1/17≈5.9%，1/18≈5.6%，1/19≈5.3%。</p>
                                <p>2.百化分记忆方法：</p>
                                <p>（1）1/2、1/4、1/5、1/10、1/20 属于常识，不需要记忆。</p>
                                <p>（2）1/4、1/8、1/16 是一半的关系，因此百分数也是一半的关系。</p>
                                <p>（3）1/3、1/6、1/12 是一半的关系，因此百分数也是一半的关系。</p>
                                <p>（4）1/7≈14.3%、1/14≈7.1%和 1/9≈11.1%、1/11≈9.1%可以成对记忆。</p>
                                <p>（5）1/13 和 1/15 需要死记硬背。</p>
                                <p>（6）1/17、1/18、1/19 对应 5.（9、6、3）%，是等差数列。5.9%最大，对应 1/17，5.3%最小，对应 1/19，5.6%在中间，对应 1/18。</p>
                                <p>（7）比如考试考 12.5%，可以想到是 1/8；百分数和分母可以相互对调，遇到 8%，是 1/12.5。因 1/8=12.5%=12.5/100→1/12.5=8/100=8%，是交叉对应的关系。</p>
                                <p>3.增长率百化分之倍数转化。利用与背过的百分数的倍数关系，实现百化分。</p>
                                <p>（1）2.5%=？25%/10=2.5%，则 2.5%=1/4÷10=1/40。</p>
                                <p>（2）67%=？6.7%=1/15，则 67%=1/15*10=1/1.5（分子一定要转换为 1 的形式）。或者看 67%≈33.3%*2≈2/3=1/1.5。</p>
                                <p>（3）22%=？因 11%=1/9，22%=2/9=1/4.5。</p>
                                <p>4.增长率百化分之取中法，如果遇到百分数左右难取舍，且选项接近，取中即可。比如 22%在 20%（1/5）和 25%（1/4）之间，可以取中为 1/4.5，是比较精确的方法，选项接近的时候，能取中尽量取中，可以提高精度。</p>
                                <p>（1）18.5%=？在 16.7%（1/6）和 20%（1/5）之间，取 1/5.5。</p>
                                <p>（2）28.7%=？在 25%（1/4）和 33.3%（1/3）之间，取 1/3.5。</p>
                                <p>5.增长率百化分之公式法，如果遇到百分数实在想不起来，或者你就不想背，那么请记住：N=100÷百分号前的数字（保留小数点后一位）。</p>
                                <p>（1）57%=？如果想不到 57%≈56%，5.6%≈1/18，则 57%≈1/1.8。可以用100/57，结果是 18 开头，则是 1/1.8，不需要除得很精确，保留小数点后一位即可。</p>
                                <p>（2）37%=？100/37，结果近似是 2.7，为 1/2.7。</p>
                                <p>（3）73%=？100/73≈1.3，为 1/1.3。不建议大家除，提前背可以节省时间。</p>
                            </div>
                        </div>
