                        <p>【注意】比重、平均数、倍数问题考点辨析：</p>
                        <p>1.比重（关键词：占、比重）：</p>
                        <p>（1）现期：A/B。</p>
                        <p>（2）基期：A/B*[（1+b）/（1+a）]。</p>
                        <p>（3）升降判断：a＞b，比重上升；a＜b，比重下降；a=b，比重不变。</p>
                        <p>（4）定量计算：两期比重差=A/B*[（a-b）/（1+a）]，先判升降，再选最小。</p>
                        <p>2.平均数（关键词：均、每、单位）：</p>
                        <p>（1）现期：A/B，后/前。</p>
                        <p>（2）基期：A/B*[（1+b）/（1+a）]。</p>
                        <p>（3）升降判断：a＞b，平均数上升；a＜b，平均数下降；a=b，平均数不变。</p>
                        <p>（4）定量计算：平均数的增长率=（a-b）/（1+b）。</p>
                        <p>3.倍数（关键词：倍）：</p>
                        <p>（1）现期：A/B。</p>
                        <p>（2）基期：A/B*[（1+b）/（1+a）]。</p>
                        <p>4.注：a 代表分子增长率；b 代表分母增长率。</p>
