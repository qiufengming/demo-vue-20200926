                        <h4 class="modal-t">二、年均增长率</h4>
                        <div class="modal-c">
                            <div class="border-radius">
                                <p>【知识点】年均增长率：国考中不会出现年均增长率计算，深圳、浙江、江西曾经考过。</p>
                                <p>1.公式：`（1+r）^n`=现期值/基期值（n 为现期和基期的年份差）。</p>
                                <p>2.识别：年均增长（增速）最快/排序；年均增长率为。</p>
                                <p>示例：某企业 2011 年的收入为 100 亿元，2014 年的收入为 200 亿元，则该企业 2011 年至 2014 年收入的年均增长率为多少？</p>
                                <p>答：年均增长率和年均增长量的意思相似，都是每年的平均值。区别是：年均增长量=总的增长量/年份差=（200-100）/3≈33.3；但年均增长率有所不同，不是用总的增长率除以年份差，指的是每年增长率都一样，2011 年为基期，收入为 100 亿元，2012 年增长 r，所以 2012 年=100*（1+r），到 2013 年又增长 r，2013 年=100*（1+r）*（1+r）=100*（1+r）²，2014 年=100*（1+r）*（1+r）*（1+r）=100*（1+r）³，所以基期*`(1+r)^³`=现期，其中 n 指的是年份差，通用公式就是`（1+r）^n`=现期/基期。</p>
                                <p>3.年均增长类问题年份差的选择：</p>
                                <p>（1）一般情况（除江苏外）：2011 年～2015 年：年份差为 4。基期：2011年；现期：2015 年。</p>
                                <p>（2）五年规划（全国都一样）：十二五期间：年份差为 5（基期往前推一年）。基期：2010 年；现期：2015 年。</p>
                                <p>（3）江苏省考（特殊）：2011 年～2015 年：年份差为 5（基期往前推一年）。基期：2010 年；现期：2015 年。</p>
                                <p>4.比较：国考只考比较，不考计算。</p>
                                <p>（1）识别：年均增长最快、年均增速排序。</p>
                                <p>（2）公式：`（1+r）^n`=现期值/基期值（n 为现期和基期的年份差）。</p>
                                <p>（3）技巧：</p>
                                <p>&nbsp;&nbsp;- ①比较：n 相同，比较 r 的大小取决于“现期/基期”，直接比较“现期/基期”。“现期/基期”越大，r 越大；“现期/基期”越小，r 越小。</p>
                                <p>&nbsp;&nbsp;- ②计算：居中代入，如果考试中居中代入都无法得到答案，建议放弃。</p>
                            </div>
                            <div class="border-radius">
                                <p>【注意】</p>
                                <p>1.选择比较好算的中间量，最多代入两次。</p>
                                <p>2.曾经江西省考有一次考试选项设置类似为27%、28%、29%、30%，没有办法代入，遇到这种题直接放弃。</p>
