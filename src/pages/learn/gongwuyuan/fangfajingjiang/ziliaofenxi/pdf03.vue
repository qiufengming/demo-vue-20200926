                    <div class="modal-wrap">
                        <h4 class="modal-t">一、现期比重</h4>
                        <div class="modal-c">
                            <div class="border-radius">
                                <p>【知识点】现期比重：</p>
                                <p>1.题型识别：……占……的比重。出现“占”，一定是比重问题，注意时间。</p>
                                <p>（1）求比重一般是占前/占后，有的题目是倒装句，如在 A 中 B 的占比，此时是 B/A，注意特殊问法。</p>
                                <p>（2）一共有 500 人，男生为 100 人，女生为 400 人，不能问男生占女生的比重，这句话是不存在的，两个独立的个体不能用占比来表述，比重的前提是有一个包含的关系，只能说男生人数是女生人数的 1/4，或者女生人数是男生人数的 4 倍。</p>
                                <p>2.公式：比重=部分/总体。</p>
                                <p>3.考查形式：</p>
                                <p>（1）已知部分和总体，求比重。</p>
                                <p>例 1：某班总共 500 人，其中男生 100 人，求男生占全班总人数的比重？</p>
                                <p>答：比重=100/500=1/5=20%。</p>
                                <p>（2）已知部分和比重，求总体。</p>
                                <p>例 2：某班男生 100 人，已知男生占全班总人数的 20%，求全班有多少人？</p>
                                <p>答：全班=100/20%=500 人。</p>
                                <p>（3）已知总体和比重，求部分。</p>
                                <p>例 3：某班总共 500 人，已知男生占全班总人数的 20%，求男生有多少人？</p>
                                <p>答：男生人数=500*20%=100 人。</p>
                                <p>4.速算技巧：截位直除。</p>
                                <p>5.概念引申（比重的特殊表述形式）：</p>
                                <p>（1）增长贡献率=部分增量/总体增量。</p>
                                <p>例：2018 年我的家庭收入 20 万元，2017 年 10 万元；其中我自己 2018 年收入 2 万元，2017 年 1 万元，问：我对家庭总收入的增长贡献率？</p>
                                <p>答：增长贡献率=部分增量/总体增量=（2-1）/（20-10）=1/10。</p>
                                <p>（2）利润率=利润/收入。数量关系：利润率=利润/成本；资料分析：利润率=利润/收入。数量关系都是小本买卖，成本非常清晰，小本生意往往用利润率=利润/成本。资料分析是统计整个大行业，成本不好统计，非常复杂，在整个大环境下，往往用收入去统计，即资料分析：利润率=利润/收入。</p>
                            </div>
                            <div class="border-radius">
                                <p>【知识点】饼形图：</p>
                                <p>1.十二点钟方向，顺时针依次排布。</p>
                                <p>2.若有总体，先看部分占总体的特殊比重；若无总体，看各部分之间倍数关系。如东部为 100，西部为 50，东、西部在饼图所占的面积为 2：1 的关系。</p>
                            </div>
                        </div>
