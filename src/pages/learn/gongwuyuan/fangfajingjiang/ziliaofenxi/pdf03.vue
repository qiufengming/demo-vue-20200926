                    <div class="modal-wrap">
                        <h4 class="modal-t">二、基期比重</h4>
                        <div class="modal-c">
                            <div class="border-radius">
                                <p>【知识点】基期比重：</p>
                                <p>1.题型识别：问题时间在材料之前，占、比重。</p>
                                <p>2.例：2017 年我的家庭总收入 B 万元，同比增长率 b，2017 年我个人收入 A 万，同比增长率 a。求：2016 年我的收入占家庭总收入的比重。</p>
                                <p>答：基期=现期/（1+r），2016 年家庭收入为 B/（1+b），个人收入为 A/（1+a），基期比重=A/（1+a）÷[B/（1+b）]=A/（1+a）*[（1+b）/B]=A/B*[（1+b）/（1+a）]。</p>
                                <p>3.计算公式：A/B*[（1+b）/（1+a）]。A：分子（部分的现期量），B：分母（整体的现期量），a：分子的增长率，b：分母的增长率。</p>
                                <p>4.速算：</p>
                                <p>（1）截位直除。</p>
                                <p>（2）先算现期比重，再看（1+b）/（1+a）大于 1 或小于 1，结合选项排除答案。</p>
                            </div>
                            <div class="border-radius">
                                <p>【注意】</p>
                                <p>1.右边式子=1.097/1.137，分子小分母大，结果小于 1；如果是 1.137/1.097，分子大分母小，结果大于 1。</p>
                                <p>2.根据“住宅投资占房地产开发投资的比重为 70.8%”，可知 A/B=70.8%。</p>
                            </div>
                        </div>
                    </div>
                    <div class="modal-wrap">
                        <h4 class="modal-t">三、两期比重</h4>
                        <div class="modal-c">
                            <div class="border-radius">
                                <p>【知识点】两期比重——升降：</p>
                                <p>1.题型识别：两个时间+比重。</p>
                                <p>例：2013 年 1～9 月，苏中工业用电量占江苏省工业用电量的比重与去年相比：</p>
                                <p>A.提高 &nbsp;&nbsp; B.降低 &nbsp;&nbsp; C.不变 &nbsp;&nbsp; D.无法判断</p>
                                <p>答：比较现期比重和基期比重的关系，是提高还是降低还是不变，这种题目就是两期比重。本题 2013 年 1～9 月比去年，去年就是 2012 年 1～9 月，两个时间，出现“占”，判定题型为两期比重问题。</p>
                                <p>2.计算公式：现期比重− 基期比重=A/B-A/B*[（1+b）/（1+a）]=A/B*[（1+a）/（1+a）-（1+b）/（1+a）]=A/B*[（1+a-1-b）/（1+a）]=A/B*[（a-b）/（1+a）]=A/B*1/（1+a）*（a-b）。由于 A/B 一定是正数，当 a＞0，1+a＞0，1/（1+a）＞0；当a＜0，a 不可能小到-100%，即 a＞-100%，则 1+a＞0，1/（1+a）＞0。因此 A/B、1/（1+a）都为正数，此时看 a-b 的大小。</p>
                                <p>3.升降判断：</p>
                                <p>（1）a＞b，比重上升。</p>
                                <p>（2）a＜b，比重下降。</p>
                                <p>（3）a=b，比重不变。</p>
                                <p>（4）a：分子（部分量）的增长率，b：分母（总体量）的增长率</p>
                                <p>4.以下问题现期均为 2019 年，请判定题型。</p>
                                <p>（1）2019 年男生占全班人数的比重是多少？出现“占”，时间为现期，现期比重。</p>
                                <p>（2）2018 年我的收入占全家总收入的比重是多少？出现“占”，时间为基期，基期比重问题。</p>
                                <p>（3）2019 年男生占全班人数的比重比女生高。出现“占”，时间为 2019 年，只有一个时间，现期比重，即男生现期比重和女生现期比重比较大小。</p>
                                <p>（4）2019 年我的收入占全家总收入的比重比上年上升。出现“占”，比重问题，2019 年比上年，有两个时间，两期比重问题。</p>
                                <p>（5）2019 年男生占全班人数的比重比 2010 年高几个百分点？出现“占”，两个时间（2019 年、2010 年），两期比重问题。</p>
                            </div>
                            <div class="border-radius">
                                <p>【注意】资料分析很少考到地理知识，大家不要在地区上抬杠。表格既然说了东部地区有北京、天津，那就默认里面包含北京、天津。</p>
                            </div>
                            <div class="border-radius">
                                <p>【知识点】两期比重：上升/下降几个百分点：</p>
                                <p>1.题型识别：两个时间+比重+上升/下降+百分点。</p>
                                <p>例：2015 年一季度，该省园区企业上缴税金占主营业务收入的比重比上年同期：</p>
                                <p>A.上升了 0.1 个百分点 &nbsp;&nbsp; B.上升了 3.1 个百分点</p>
                                <p>C.下降了 0.1 个百分点 &nbsp;&nbsp; D.下降了 3.1 个百分点</p>
                                <p>答：出现“占”字，是比重，给了 2015 年和同比，两期比重问题。直接用现期比重- 基期比重，比如 2015 年一季度占比是 5%，去年同期占比是 4%，则现在的占比比过去的占比上升了 5-4=1 个百分点。</p>
                                <p>2.计算公式：现期比- 基期比。</p>
                                <p>（1）A/B-A/B*[（1+b）/（1+a）]=A/B*[（a-b）/（1+a）]=A/B*[1/（1+a）]*（a-b）＜|a-b|。</p>
                                <p>（2）推导：把式子拆成三个部分，A/B 是一个部分，1/（1+a）是一个部分，（a-b）是一个部分。A/B 是现期比重，比重=部分（小数）/总体（大数），肯定小于 1。再看 1/（1+a），如果 a＞0，则 1/（1+a）＜1；如果 a＜0，则 1/（1+a）＞1；a 的取值不同，会有两种情况。我们做的是资料分析，根据我国的发展数据可知，比重 A/B 往往很小，最高达到 50%，所以 A/B＜1，如果 a＞0，A/B*[1/（1+a）]肯定＜1；在资料分析中，如果 a＜0，1/（1+a）也只会比 1 大一丢丢，综合起来，A/B*[1/（1+a）]还是比 1 小。</p>
                                <p>（3）记住结论：A/B*[1/（1+a）]永远＜1，再乘以（a-b），所以 A/B*[1/（1+a）]*（a-b）＜|a-b|。</p>
                                <p>3.解题步骤：</p>
                                <p>（1）判方向（a＞b，上升；a＜b，下降）。</p>
                                <p>（2）定大小：小于|a-b|，算出差值，选一个比差值小的即可。</p>
                                <p>4.注意：a 对应分子（部分量）的增长率；b 对应分母（总体量）的增长率。</p>
                            </div>
                            <div class="border-radius">
                                <p>【注意】比重梳理：（出现“占”/利润率/增长贡献率）</p>
                                <p>1.现期比重→占→占前（A）/占后（B）。</p>
                                <p>2.基期比重→占、基期→A/B*[（1+b）/（1+a）]。</p>
                                <p>3.（1）两期比重→比重、升降→a＞b，比重升，a＜b，比重降；</p>
                                <p>（2）两期比重→比重、百分点→先判升降，再选小。</p>
                            </div>
                            <div class="border-radius">
                                <p>【注意】比重小结：</p>
                                <p>1.现期比重：</p>
                                <p>（1）识别：问题时间与材料一致，占，比重。</p>
                                <p>（2）公式：比重=部分/总体；总体=部分/比重；部分=总体*比重。</p>
                                <p>（3）速算：截位直除法。</p>
                                <p>2.基期比重：</p>
                                <p>（1）识别：问题时间在材料之前，占，比重。</p>
                                <p>（2）公式：[A/（1+a）]÷[B/（1+b）]=A/B*[（1+b）/（1+a）]。</p>
                                <p>（3）速算：</p>
                                <p>&nbsp;&nbsp;- ①截位直除。</p>
                                <p>&nbsp;&nbsp;- ②先计算现期比重，再判断大小。</p>
                                <p>3.两期比重：</p>
                                <p>（1）识别：两个年份，一个比重。</p>
                                <p>（2）公式：A/B*[（a-b）/（1+a）]。</p>
                                <p>（3）升降判断：比较部分与总体增长率，部分大则升、小则降。</p>
                                <p>（4）速算：比重差的绝对值小于增长率之差的绝对值，即|A/B*[（a-b）/（1+a）]|＜|a-b|。</p>
                            </div>
