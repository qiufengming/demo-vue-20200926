                <h3 class="text-c section-t" @click="sectionOpenClose('s8')">第八节 倍数</h3>
                <div class="section-c" v-show="sectionOpen.s8">
                    <div class="border-radius">
                        <p>基本术语：</p>
                        <p>倍数用来表示两者的相对关系。</p>
                        <p>若 A 是 B 的 n 倍，则 n=r+1（r 指 A 与 B 相比的增长率）。</p>
                    </div>
                    <div class="border-radius">
                        <p>【知识点】倍数：</p>
                        <p>1.题型识别：时间+倍数。</p>
                        <p>2.现期倍数：A/B，与现期比重、现期平均数的公式相同。</p>
                        <p>例：2017 年，小明收入 10 个亿，马云收入 2 个亿，小明收入是马云的多少倍？</p>
                        <p>答：小学难度，A/B=10/2=5 倍。</p>
                        <p>3.基期倍数：A/B*[（1+b）/（1+a）]，与基期比重、基期平均数的公式相同。</p>
                        <p>例：2017 年，小明收入 A 个亿，同比增长率为 a，马云收入 B 个亿，同比增长率为 b，2016 年小明收入是马云的多少倍？</p>
                        <p>答：2016 年小明/2016 年马云=A/（1+a）÷[B/（1+b）]=A/（1+a）*[（1+b）/B]=A/B*[（1+b）/（1+a）]。</p>
                        <p>4.速算技巧：截位直除；先计算 A/B，再看（1+b）/（1+a）与 1 的关系。</p>
                        <p>5.易错点：</p>
                        <p>（1）A 是 B 的几倍：A/B，是“真”倍数，直接除。</p>
                        <p>（2）A 比 B 增长（多）几倍：（A-B）/B=A/B-1，是“假”倍数，本质上是增长率，除完要减 1。倍数=r+1，A/B-1=r。</p>
                        <p>（3）A 超过 B 的 n 倍：超过就是“＞”，A＞B*n→A/B＞n。</p>
                        <p>6.注意：倍数没有两期问题，只有现期倍数和基期倍数。</p>
                    </div>
