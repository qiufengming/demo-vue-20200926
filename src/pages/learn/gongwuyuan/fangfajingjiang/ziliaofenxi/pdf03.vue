                <h3 class="text-c section-t" @click="sectionOpenClose('s4')">第四节 一般增长率</h3>
                <div class="section-c" v-show="sectionOpen.s4">
                    <div class="border-radius">
                        <p>基本术语：</p>
                        <p>增长率</p>
                        <p>增长率是用来表述基期量与现期量变化的相对量。增长率又称增速、增幅或增长幅度、增值率等。增长率为负时表示下降，下降率也可直接写成负的增长率。</p>
                        <p>百分数与百分点</p>
                        <p>百分数：用来反映量之间的比例关系。</p>
                        <p>百分点：用来反映百分数的变化。</p>
                        <p>增长率与倍数</p>
                        <p>增长率指比基数多出的比率；倍数指两数的直接比值。</p>
                        <p>若 A 是 B 的 n 倍，则 n=r+1（r 指 A 与 B 相比的增长率）。</p>
                        <p>成数与翻番</p>
                        <p>成数：几成相当于十分之几。</p>
                        <p>翻番：翻一番为原来的 2 倍；翻两番为原来的 4 倍；依次类推，翻 n 番为原来的 2<sup>n</sup>倍。</p>
                        <p>增幅、降幅与变化幅度</p>
                        <p>增幅一般就是指增长率，有正有负。</p>
                        <p>降幅指下降的幅度，降幅比较大小时，前提必须为下降。</p>
                        <p>变化幅度指增长或下降的绝对比率，变化幅度比较大小时用增幅（降幅）的绝对值。</p>
                    </div>
                    <div class="border-radius">
                        <p>【知识点】常见词语辨析：</p>
                        <p>1.“百分数”与“百分点”：</p>
                        <p>（1）百分数表示两个量的比例关系，用除法计算。</p>
                        <p>（2）百分点表示百分数的变化（差值），用加减法计算。</p>
                        <p>（3）考查形式：给出一个百分数和百分点，求另一个百分数。出现百分点，考虑百分数的加减计算。</p>
                        <p>（4）补例：</p>
                        <p>&nbsp;&nbsp;- ①甲班共有 50 人，女生人数 30 人，问女生占全班人数的比重？</p>
                        <p>&nbsp;&nbsp;- 答：女生/全班=30/50=60%，60%是一个百分数，是用除法得到的</p>
                        <p>&nbsp;&nbsp;- ②2017 年某地区小麦产量同比增长率为 8%，较玉米产量的增长率低了 5 个百分点，则 2017 年该地区玉米产量的增长率为？</p>
                        <p>&nbsp;&nbsp;- 答：小麦增长率比玉米低 5 个百分点，即玉米增长率-小麦增长率=5 个百分点，已知小麦增长率为 8%，则玉米增长率为 13%。</p>
                        <p></p>
                        <p>2.“增长率”与“倍数”：增长率指比基数多出的比率；倍数指两数的直接比值。比如 2 是 1 的 2 倍（“真”倍数，直接除），2 比 1 多/增加（2-1）/1=1倍（“假”倍数，本质上是增长率，先减再除）。</p>
                        <p>（1）A 是 B 的多少倍？倍数=A/B。</p>
                        <p>（2）A 比 B 多/增长多少（%）？增长率=（A-B）/B=A/B-1。</p>
                        <p>（3）A/B=（A/B-1）+1，即倍数=增长率+1。例：150 是 100 的 1.5 倍；150比 100 多（150-100）/100=0.5 倍，即增长率为 50%。</p>
                        <p>（4）高频易错点：</p>
                        <p>&nbsp;&nbsp;- ①2019 年比 2018 年增长了 300%。增长率为 300%，即增长了 3 倍，则 2019年是 2018 年的 4 倍。</p>
                        <p>&nbsp;&nbsp;- ②2019 年比 2018 年增长了 2 倍。增长率为 2=200%，则 2019 年是 2018 年的 3 倍。</p>
                        <p>&nbsp;&nbsp;- ③2019 年比 2018 增长了 3/10。增长率为 3/10=0.3=30%，则 2019 年是 2018 年的 3/10+1=13/10=1.3 倍。</p>
                        <p>&nbsp;&nbsp;- ④思考：2019 年比 2018 年增长了 1.8 倍，2019 年是 2018 年的（ ）倍？</p>
                        <p>&nbsp;&nbsp;- 答：增长了 1.8 倍，说明增长率为 1.8=180%，则 2019 年是 2018 年的 2.8倍。</p>
                        <p></p>
                        <p>3.成数、翻番：</p>
                        <p>（1）成数：几成相当于十分之几。三成是 3/10=30%，五成是 5/10=50%；三成多为 30%～40%，如果计算出来是 40<sup>+</sup>%，则不能称之为三成多。</p>
                        <p>（2）翻番：翻一番为原来的 2 倍；翻两番为原来的 4 倍；以此类推，翻 n 番为原来的 2<sup>n</sup>倍。比如斗地主，假设底分为 10000，“抢地主”底分乘 2，“炸弹”再乘 2，“王炸”再乘 2，10000*2*2*2。</p>
                        <p></p>
                        <p>4.“增幅”“降幅”“变化幅度”：</p>
                        <p>（1）增幅（增长率）：可正可负，带符号比。增幅=r=增长速度=增速，只是字不一样，本质上没有区别。如果增长率为 10%，那么增幅就是 10%；如果增长率为-5%，那么增幅就是-5%。</p>
                        <p>（2）降幅：必须为负，比较绝对值。如果 r=-10%，则降幅为 10%，“降”就代表负号；如果 r=-5%，则降幅为 5%。如果 r=20%，则没有降幅的说法</p>
                        <p>（3）变化幅度：可正可负，比较绝对值。变化幅度即增长率的绝对值，如果 r=10%，则变化幅度是 10%；如果 r=-10%，则变化幅度也是 10%。</p>
                    </div>
                    <div class="modal-wrap">
                        <h4 class="modal-t">一、计算</h4>
                        <div class="modal-c">
                            <div class="border-radius">
                                <p>【知识点】一般增长率：</p>
                                <p>1.计算类：题目给出百分点的变化，求增长率，用加减计算。</p>
                                <p>2.比较类。</p>
                            </div>
                            <div class="border-radius">
                                <p>【注意】高频易错点（高减低加）：</p>
                                <p>1.2017 年收入 10 万元，同比增长 10%，增速比去年提高 5 个百分点。则 2016 年的增长率为？</p>
                                <p>答：比如我今年有 10 元钱，比去年多 5 元，说明去年更低。同理，增速比去年高，则去年更低，用减法，2016 年增长率为 10%-5%=5%。出现“提高/上升”用减法。</p>
                                <p>2.2017 年收入 10 万元，同比增长 10%，增速比去年回落 5 个百分点。则 2016 年的增长率为？</p>
                                <p>答：回落即掉落，增速比去年低，说明去年更高，用加法，2016 年增长率为 10%+5%=15%。出现“回落/下降/减少”用加法。</p>
                                <p></p>
                                <p>1.2017 年收入 10 万元，同比下降 10%，降幅比去年扩大 5 个百分点。则 2016 年的降幅为？增长率为？</p>
                                <p>答：2017 年同比下降 10%，即降幅为 10%，降幅比去年扩大，高减低加，“扩大”用减法，则 2016 年降幅为 10%-5%=5%，转化成增长率为-5%。可以画图理解，从-5%到-10%，亏得越来越多，是降幅扩大</p>
                                <p>2.2017 年收入 10 万元，同比下降 10%，降幅比去年收窄 5 个百分点。则 2016 年的降幅为？增长率为？</p>
                                <p>答：收窄即变小、变窄，是降低的意思。2017 年降幅为 10%，降幅比去年收窄，高减低加，“收窄”用加法，则 2016 年降幅为 10%+5%=15%，转化成增长率为-15%。可以画图理解，从-15%到-10%，亏得越来越少，是降幅收窄。</p>
                                <p>3.“增速”直接带符号，用“高减低加”正常做。“降幅”先不带符号算降幅，后面再添“负号”。</p>
                            </div>
                            <div class="border-radius">
                                <p>【知识点】计算类：给具体量（现期量、基期量、增长量）。</p>
                                <p>1.识别：增长/下降+%、几成；增长速度、增长幅度。</p>
                                <p>2.公式：r=增长量/基期量=增长量/（现期-增长量）=（现期- 基期）/基期，公式之间是相通的。</p>
                                <p>（1）最本质的公式：r=增长量/基期量。比如现在体重为 120 斤，过去体重为 100 斤，说明增长了 20 斤，增长率为 20/100=20%。</p>
                                <p>（2）已知增长量和现期：r=增长量/（现期-增长量）。</p>
                                <p>（3）已知现期和基期：r=（现期- 基期）/基期。</p>
                                <p>3.速算：截位直除或估算。</p>
                            </div>
                            <div class="border-radius">
                                <p>【注意】若不喜欢减法，则可以求（现期- 基期）/基期＞10%，现期- 基期＞基期 *10% ， 现 期 ＞ 1.1* 基 期 ， 一 个 数 乘 以 1.1 考 虑 错 位 相 加 ， 如 1127.6*1.1=1127.6*（1+10%）=1127.6+112.76。</p>
                            </div>
                            <div class="border-radius">
                                <p>【注意】小结：</p>
                                <p>1.给现期和基期，正常做除法。</p>
                                <p>2.给出百分点，考虑百分数加减计算。</p>
                                <p>3.问固定某几年的增长率超过百分之几，考虑对公式进行优化，化繁为简，解题的目的不是当计算器，而是解决问题。</p>
                            </div>
                        </div>
                    </div>
