                    <div class="border-radius">
                        <p>基本术语：</p>
                        <p>比重指部分在总体中所占的比率，有时也用贡献率、利润率等表述方式。</p>
                        <p>增长贡献率指部分增量在总体增量中所占的比例。</p>
                        <p>资料分析中的利润率特指利润在收入中的占比。</p>
                    </div>
                    <div class="border-radius">
                        <p>【注意】比重：</p>
                        <p>1.现期比重。</p>
                        <p>2.基期比重。</p>
                        <p>3.两期比重比较与计算。</p>
