                        <h4 class="modal-t">一、间隔增长率</h4>
                        <div class="modal-c">
                            <div class="border-radius">
                                <p>【知识点】间隔增长率：之前学的增长率都是连续的状态，比如 2019 年比2018 年，这是同比增长率，2019 年 7 月比 2019 年 6 月增长，这是环比增长率。间隔增长率的时间一定是隔开的，已知：某企业 2018 年主营业务收入同比增长率为 `r_1`，2017 年同比增长率为 `r_2`。求：该企业 2018 年主营业务收入比 2016 年的增长率为多少？时间不连续，中间间隔一年，求增长率，就是间隔增长率。</p>
                                <p>1.公式推导：`r=r_1+r_2+r_1*r_2`。已知 2018 年同比增长率为 `r_1`，2017 年同比增长率为 `r_2`。求：2018 年比 2016 年的增长率。没有 2016 年的数据，2018 年是现期，2016 年是基期，求增长率可以用（2018 年-2016 年）/2016 年。假如 2016年的值用 2016 表示，2017 年同比增长率为 `r_2`，2017 年的值为`基期*（1+r）=2016*（1+r_2）`，2018 年同比增长率为 `r_1`，`2018=2017*（1+r_1）=2016*（1+r_1）*（1+r_2）`，所以（2018 年-2016 年）/2016 年=[2016*（1+`r_1`）*（1+`r_2`）-2016]/2016=`1+r_1+r_2+r_1*r2-1`=`r_1+r_2+r_1*r_2`。</p>
                                <p>2.`r_1`、`r_2`怎么找？`r_1`是现期时间的增长率，`r_2`是间隔时间的增长率。</p>
                                <p>（1）例：2018 年比 2016 年增长了百分之几？`r_1`：2018 年的同比增长率。`r_2`：2017 年的同比增长率。</p>
                                <p>（2）练习：</p>
                                <p>&nbsp;&nbsp;- ①2017 年比 2015 年增长了百分之几？2017 年和 2015 年中间间隔 2016 年，`r_1`：2017 年的同比增长率。`r_2`：2016 年的同比增长率。</p>
                                <p>&nbsp;&nbsp;- ②2014 年比 2012 年增长了百分之几？2014 年和 2012 年中间间隔 2013 年，`r_1`：2014 年的同比增长率。`r_2`：2013 年的同比增长率。但是没有必要区分 `r_1`和`r_2`，加法和乘法都有交换律，`r=r_1+r_2+r_1*r_2=r_2+r_1+r_2*r_1`。</p>
                                <p>3.公式咋算？</p>
                                <p>（1）不计算：结合选项排除。例：15%+18%+15%*18%≈？</p>
                                <p>A.35.7% &nbsp;&nbsp; B.30.9% &nbsp;&nbsp; C.28.9% &nbsp;&nbsp; D.41.6% </p>
                                <p>答：乘法部分很难算，估算在前，如果估算不了，再想其他办法，15%+18%=33%，还要加两个数的乘积，结果一定大于 33%，排除 B、C 项。15%*18%只有一点点，选择 A 项。</p>
                                <p>（2）可估算：`r_1`、`r_2`的绝对值均小于 10%，`r_1`*`r_2`可以忽略。10%*10%=1%，如果 `r_1`、`r_2`的绝对值均小于 10%，`r_1`*`r_2`就 1%都不到了，可以忽略不计。</p>
                                <p>&nbsp;&nbsp;- ①：5%+8%+5%*8%≈？5%+8%=13%，5%*8%很容易计算，但如果是 5.13%*8.28%，就很难计算了。5%、8%均小于 10%，5%*8%可以直接忽略，只需要计算加法部分。</p>
                                <p>&nbsp;&nbsp;- ②：5%+36%+5%*36%≈？5%＜10%，但是 36%＞10%，只有当 `r_1`、`r_2` 的绝对值均小于 10%才可以忽略 `r_1`*`r_2`，因此 5%*36%不能忽略，5%*36%=1/20*36%=1.8%，原式=41%+1.8%=42.8%。或者 36%≈33%≈1/3，5%*1/3=1<sup>+</sup>%。</p>
                                <p>&nbsp;&nbsp;- ③：17.9%+20.2%+17.9%*20.2%≈？17.9%+20.2%=38.1%，17.9%、20.2%均大于 10%，不能忽略 17.9%*20.2%，20.2%≈20%=1/5，17.9%*1/5，或者直接看成18%*20%。注意：a%*b%=（a*b/100）%。17.9%*20.2%≈18%*20%=（18*20/100）%=3.6%。</p>
                                <p>（3）不得不算：结合特殊分数。</p>
                                <p>&nbsp;&nbsp;- ①28%+25%+28%*25%=？28%+25%+28%*25%=53%+28%*1/4=60%。</p>
                                <p>&nbsp;&nbsp;- ② -18%+5%+ （ -18% ） *5%= ？ -18%+5%+ （ -18% ） *5%=-13%+ （ -18% ）*1/20=-13%-1<sub>-</sub>%=-14<sub>-</sub>%。</p>
                                <p>&nbsp;&nbsp;- ③-18%+（-5%）+（-18%）*（-5%）=？-18%+（-5%）+（-18%）*（-5%）≈ -23%+1%=-22%。</p>
                                <p>4.间隔增长率总结：</p>
                                <p>（1）识别：中间隔一年，求增长量。比如 2019 年比 2017 年，2016 年比 2014年。只有中间间隔一年的可以用间隔增长率公式。如果问 2018 年相比 2008 年的增长率，用不了间隔增长率，就是一个普通的增长率，会给 2018 年的现期值和 2008 年的基期值，用（2018 年-2008 年）/2008 年。</p>
                                <p>（2）公式：`r=r_1+r_2+r_1*r_2`。</p>
                                <p>（3）技巧：</p>
                                <p>&nbsp;&nbsp;- ①`r_1`、`r_2`绝对值均小于 10%时，`r_1`*`r_2`可忽略。</p>
                                <p>&nbsp;&nbsp;- ②结合选项或百化分。</p>
                            </div>
                            <div class="border-radius">
                                <p>【知识点】间隔倍数：</p>
                                <p>1.识别：中间隔一年，求倍数。</p>
                                <p>2.公式：间隔倍数=间隔增长率+1</p>
                                <p>3.做题步骤：</p>
                                <p>（1）先算间隔增长率 `r=r_1+r_2+r_1*r_2`。</p>
                                <p>（2）再加 1。</p>
