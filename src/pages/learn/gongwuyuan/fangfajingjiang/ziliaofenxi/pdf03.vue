                                <p>1.识别：间隔一年，求基期量。给2018年问2016年的量，间隔基期。</p>
                                <p>2.公式：间隔基期量=现期量/（1+间隔增长率）。</p>
                                <p>3.做题步骤：</p>
                                <p>（1）先求间隔增长率`r=r_1+r_2+r_1*r_2`。</p>
                                <p>（2）计算：现期量/（1+间隔r）。</p>
                            </div>
                            <div class="border-radius">
                                <p>【知识点】间隔增长量：</p>
                                <p>1.识别：中间隔一年，求增长量。</p>
                                <p>2.公式：算出间隔增长率，再用百化分计算。</p>
                                <p>3.做题步骤：</p>
                                <p>（1）先算间隔增长率`r=r_1+r_2+r_1*r_2`</p>
                                <p>（2）再百化分。</p>
                                <p>（3）间隔增长率＞0，增长量=现期/（n+1）；间隔增长率＜0，减少量=现 期/（n-1）。</p>
                            </div>
                            <div class="border-radius">
                                <p>【注意】间隔+增长率、倍数、基期量、增长量小结：例1～5的共同点：都是间隔年份，都算了间隔增长率。只不过有些题只需要算间隔增长率，有些题算的是间隔倍数、间隔基期量、间隔增长量。以后看到有间隔年份的题，先把年均增长率算出来。</p>
                                <p>1.题型识别：中间隔一年，求增长率/倍数/基期量/增长量。</p>
                                <p>2.公式：`r_间=r_1+r_2+r_1*r_2`；间隔倍数=`r_间`+1；间隔基期量=现期量/（1+`r_间`）；间隔增长率百化分，间隔增长量=现期/（n+1）。</p>
                                <p>3.速算技巧：</p>
                                <p>（1）结合选项排除。</p>
                                <p>（2）`r_1`、`r_2`的绝对值均小于10%，`r_1`*`r_2`可以忽略。</p>
                                <p>（3）特殊分数。</p>
