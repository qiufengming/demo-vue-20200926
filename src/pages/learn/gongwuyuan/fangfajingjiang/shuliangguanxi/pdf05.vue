                    <h3 class="text-c section-t" @click="sectionOpenClose('s4')">第四节 工程问题</h3>
                    <div class="section-c" v-show="sectionOpen.s4">
                        <div class="border-radius">
                            <p>【知识点】工程问题：</p>
                            <p>1.三量关系：总量=效率*时间。</p>
                            <p>（1）总量：工作到底做多少，例如：上课 180 分钟、做多少道题，都是工
                                作量。</p>
                            <p>（2）效率：单位时间内做的工作量，其中单位时间要看题干条件，有时为
                                一天的效率、有时为 1 小时的效率、有时为 1 分钟的效率。</p>
                            <p>（3）时间：总量/效率，例如：唐宋老师需要搬砖 1000 块，每小时搬砖 200
                                块，则 5 小时搬完。</p>
                            <p>（4）已知其中的两个量，一定可以求出第三个量。绝大多数的工程问题（90%）
                                都是问时间，此时只要找到总量和效率，效率=总量/时间。</p>
                            <p>2.考查题型：一共分为四种题型，前三种为主要的题型，第四种为特殊的考
                                法。</p>
                            <p>（1）给完工时间型：工程问题的基础，比较常见，难度不高，比较简单。</p>
                            <p>&nbsp;&nbsp;- ①赋总量（完工时间的公倍数）。</p>
                            <p>&nbsp;&nbsp;- ②算效率：效率＝总量/时间。</p>
                            <p>&nbsp;&nbsp;- ③根据工作过程列方程或式子。</p>
                            <p>&nbsp;&nbsp;- ④引例.要折叠一批纸飞机，若甲单独折叠要半个小时完成，乙单独折叠需
                                要 45 分钟完成。若两人一起折，需要多少分钟完成？</p>
                            <p>A.10 &nbsp;&nbsp; B.15 &nbsp;&nbsp; C.16 &nbsp;&nbsp; D.18</p>
                            <p>【解析】引例.题干没有告诉纸飞机的总量，一般 90%的工程问题不会告诉
                                工程总量，此时可以赋值（假设一个工作量）。题干没有纸飞机的总量，题干也
                                没有问纸飞机的总量，说明和纸飞机的总量和答案没有关系，如果和答案有关系，
                                但是题目没给，此时该题就无法做出来，是一道错题。（1）为了好算，尽量假设
                                总量为完工时间的公倍数，半个小时为 30 分钟，30 和 45 的公倍数为 90，假设
                                总量为 90 个；（2）根据总量计算效率，P 甲=90/3=3 个/分钟，P 乙=90/45=2 个/
                                分钟；（3）根据工作过程列式子，90 个/[（3+2）个/分钟]=18 分钟，对应 D 项。
                                【选 D】</p>
                            <p>（2）给效率比例型。</p>
                            <p>（3）给具体单位型。</p>
                            <p>（4）同时开始同时结束。</p>
                        </div>
                        <div class="border-radius">
                            <p>【例 2】（2019 河北）甲、乙两队单独完成某项工程分别需要 10 天、17 天。
                                甲队与乙队按天轮流做这项工程，甲队先做，最后是哪队第几天完工？</p>
                            <p>A.甲队第 11 天 &nbsp;&nbsp; B.甲队第 13 天 &nbsp;&nbsp; C.乙队第 12 天 &nbsp;&nbsp; D.乙队第 14 天</p>
                            <p>【解析】例 2.该题比较特殊，国考和联考都考过轮流的问题，但是近五年
                                每怎么考，该题（2019 年河北）考到了轮流。（1）已知“甲、乙两队单独完成
                                某项工程分别需要 10 天、17 天”，10 和 17 之间没有公约数，所以设工程总量为
                                10*17=170；（2）算效率，P 甲=17，P 乙=10；（3）难点在于工作过程比较特殊，已
                                知“甲队与乙队按天轮流做这项工程”，第一天只有甲做，第二天只有乙做，一
                                个循环中，第一天甲干，第二天乙干，看有几个循环，170/（17+10）=170/27=6……
                                8。这里的 6 为循环的个数，余数 8 和被除数是一个概念，总量为 170 份工作，
                                则剩下 8 份工作。前面 6 个循环均为甲乙、甲乙……，剩下的 8 份开启第 7 个循
                                环，每个循环开始甲开始做，所以最后的 8 份工作由甲做完，排除 C、D 项；每
                                个循环用时 2 天，所求=2*6 天+甲做的天数=12 天+1 天=13 天，所以最后甲队第
                                13 天完工，对应 B 项。【选 B】</p>
                            <p></p>

                            <p>【注意】</p>
                            <p>1.千万不要写为 170/27=6`8/27`个循环，循环是不均匀的，不是每一分每一秒都
                                是甲、乙平摊的。`8/27`意为平均分成 27 份，取了其中的 8 份。这样做题是不对的，
                                相当于把循环看成了均匀的概念，一个循环中，可能前面快一点后面慢一点，或者后面快一点前面慢一点。时间并不一定可以写成分数。循环只有一种写法：总
                                量/循环的量=若干个循环……余数。这样思考：整数的循环对应整数的天数，余
                                下的部分看是谁先干，该题中是甲先干，甲的效率为 17，1 天可以干完 8 份，所
                                以用时 1 天。</p>
                            <p>2.如果余数为 18，甲的效率只有 17，甲 1 天干不完，还需要乙再做 1 天，
                                相当于甲再第 13 天做了 17 份，剩下的 1 份，需要乙在第 14 天才能做完。此时
                                选择 D 项。</p>
                            <p>3.给完工时间型，做法比较固定，一般给出两个人或者三个人单独的完工时
                                间，根据完工时间赋值工作总量，然后计算出每个人的效率，最后分析工作过程。</p>
                        </div>
                        <div class="border-radius">
                            <p>【知识点】给效率比例型：</p>
                            <p>1.赋效率（满足比例即可）。</p>
                            <p>2.算总量：总量=效率*时间。</p>
                            <p>3.根据工作过程列方程或式子。</p>
                            <p>4.引例.甲和乙的效率比为 2：3，甲、乙合作完成一项工程需要 10 天，如
                                果甲单独做这项工程需要多少天？</p>
                            <p>A.15 &nbsp;&nbsp; B.20 &nbsp;&nbsp; C.25 &nbsp;&nbsp; D.30</p>
                            <p>【解析】引例.发现题干只有 1 个时间，两个数以上才能求公倍数，没有告
                                诉效率的具体值，只给了比例，（1）根据“甲和乙的效率比为 2：3”，可以赋值
                                效率，甲的效率为 2，乙的效率为 3；（2）已知“甲、乙合作完成一项工程需要
                                10 天”，工作总量=（2+3）*10 天=50；（3）分析工作过程，问“甲单独做这项工
                                程需要多少天”，50/2=25，对应 C 项。【选 C】</p>
                            <p>5.给效率比例的几种不同形式：</p>
                            <p>（1）直接型：甲的效率是乙的 1.5 倍。可以写甲的效率为 1，乙的效率为
                                1.5，但是建议写成整数，甲/乙=1.5=3/2，则甲的效率为 3，乙的效率为 2。</p>
                            <p>（2）间接型：甲 3 天的工作量相当于乙 4 天的工作量。题干没有出现“效
                                率”这个词，只说“工作量……”。“相当于”就是等于的意思。`P_甲*3 天=P_乙*4天`，
                                乘积一定的情况下，时间越小，效率越高，时间和效率成反比，`P_甲：P_乙=4：3`。此时可以设甲的效率为 4，乙的效率为 3。还不熟悉的时候，可以一步一步分
                                析，题目做多了以后，量一定，时间和效率成反比，直接写 `P_甲：P_乙=4：3`。</p>
                            <p>（3）特殊型：某农场有 36 台收割机，或者 36 名工人。有若干名工人/机器，
                                其中“若干”为具体的数量。每个工人/每台机器的效率没有区别（实际情况是
                                有区别的，但是题目中默然效率是一样的）。一般为了方便，一个人的效率为 1，
                                36 个人的效率为 36；每台机器的效率为 1，36 台机器的效率为 36。</p>
                            <p>（4）考查最多的为间接型。直接型就是按照比例关系直接设效率；间接型
                                一般是总量相当，时间和效率反过来；特殊型一般设每个人/每台机器效率为 1。</p>
                        </div>
                        <div class="border-radius">
                            <p>【注意】</p>
                            <p>1.例如：放寒假，本来可以正常写完作业，但是在写作业的过程中遇到了春
                                节，有 5 天的时间无法写作业，最后 8 天的时候拼命赶作业，如果按期把作业交
                                上去，每天需要写多少作业，最后 8 天要写的作业：正常 8 天要写的作业+5 天
                                没写的作业=13 天的作业。</p>
                            <p>2.该题也是 2018 年国考的题目，辽宁省考只不过是改了一下数字，做题思
                                路和国考题是一模一样的。</p>
                        </div>
                        <div class="border-radius">
                            <p>【知识点】给具体单位型：例如：搬 1000 块砖，此时已知工作量，但是例
                                1～例 5 没有出现过具体的工作量（具体单位），前面用赋值的方法把工作量假设
                                出来，然后做题。出现总工作量或者 200 块砖/小时，出现具体工作量或者具体
                                效率，此时可以设未知数列方程。为什么还要设未知数，因为题干不可能把所有
                                的数值均告诉我们，例如：已知甲每小时比乙多搬砖 200 块，甲、乙的效率未知，
                                但是已知甲、乙效率的差，此时设乙为 x，甲为 200+x。或者一直甲效率和乙效
                                率的和为 200，则设一个为 x，另一个为 200-x。</p>
                            <p>1.设未知数（设小不设大或设中间量）。</p>
                            <p>2.找等量关系列方程。等量关系只有一种：量=效率*时间。</p>
                        </div>
                        <div class="border-radius">
                            <p>【知识点】同时开始同时结束：</p>
                            <p>1.典型情况：三个人做两项工作，要求同时开工同时结束。例如：第一项工
                                作甲负责，第二项安排乙来负责，丙一会帮甲干，一会帮乙干，要求两项工作同
                                时开工同时结束。</p>
                            <p>2.核心思路：所有的人全程没有休息，工作时间＝两项工作的工作量之和/
                                三个人的效率之和。简单记忆：工作时间=工作量之和/效率之和。例如：A 项工
                                作为 100 块砖，B 项工作为 200 块砖，安排甲、乙、丙三人去做，甲每分钟干 3
                                块，乙每分钟干 4 块，丙每分钟干 5 块，甲负责干 A 项工作，乙负责干 B 项工作，
                                丙是负责帮忙的，整个过程中，中途不休息，三人的 t 一样，`P_甲*t+P_乙*t+P_丙*t=A 量+B 量`，3t+4t+5t=100+200=300，t=300/（3+4=5）=25 分钟。</p>
                        </div>
