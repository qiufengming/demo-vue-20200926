                            <p>【知识点】画图法：</p>
                            <p>1.若条件或问题不便于代入公式计算，则考虑画图。
                            </p>
                            <p>2.例如：只参加 A；参加 A、B 但不参加 C；或者缺少代公式必要的数据。如
                                图所示：左边的圆圈为 A，阴影部分为只 A，但是阴影部分在公式中是不存在的，
                                公式中的 A 为“圆圈”代表的区域，出现和公式表述不同的，就用画图法。</p>
                            <p>3.先画圈，再代数；从里到外，注意去重。例如：A∩B=5 人，A 的人数为
                                12 人，先把 5 人标在中间，再把 12 人标在外面，7 人标在里面。不要把 12 人和
                                5 人标在一起，因为 12 人包含 5 人。</p>
