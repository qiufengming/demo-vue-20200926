                                集”的概念大家了解即可，因为公考不考，这里是为了和中学的知识点产生联系。
                                35+38-30=50-（ ），一般来说，选项尾数不同，利用尾数法，尾数 3=尾数 0-（ ）的尾数，则（ ）的尾数为 7。</p>
                            <p>4.考试中把“补集”理解为“都不”。就是哪个集合都不属于，这样描述比
                                较接地气。只理解交集（∩）的概念即可。
                                </p>
