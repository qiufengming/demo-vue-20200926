                        <div class="border-radius">
                            <p>【知识点】三集合非标准型：是热门考点，为重中之重。公考第一次考查这
                                个公式时，完全用高中的知识储备来做。非标准是因为推导方式比较特殊。</p>
                            <p>1.公式：A+B+C-满足两项-满足三项*2=总数-都不。</p>
                            <p>2.推导：如下图所示，满足的两项为 x、y、z 区域，x 为 A、B 相交，y 为 B、
                                C 相交，z 为 A、C 相交，其中 m 为 A∩B∩C。A+B+C 中，x 被 A 算了一遍，被 B
                                算了一遍，算了两遍，只重复了一遍。同理，y 被 B、C 各算了一遍，z 被 A、C
