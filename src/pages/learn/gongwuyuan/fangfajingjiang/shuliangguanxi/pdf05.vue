                    <h3 class="text-c section-t" @click="sectionOpenClose('s2')">第二节  倍数特性法</h3>
                    <div class="section-c" v-show="sectionOpen.s2">
                        <div class="border-radius">
                            <p>【注意】倍数特性：用于排除，使用的方法多。</p>
                        </div>
                        <div class="modal-wrap">
                            <h4 class="modal-t">一、基础知识</h4>
                            <div class="modal-c">
                                <div class="border-radius">
                                    <p>【知识点】基础知识：</p>
                                    <p>1.A=B*C，B、C 均为整数，A 既能被 B 整除，又能被 C 整除。求的量能写成 2 个数的乘积，一般思路是计算出 2 个数，这是简答题/填空题的思路，公务员考试是选择题，假设其中一个数为 3，可以根据 3 的倍数可以排除答案，假设答案分别为 A.8、B.9、C.10、D.11，只有 B 项满足 3 的倍数，解题时无需计算出 2 个数，即不需要解完题目，可以用倍数特性解题。</p>
                                    <p>2.适用于：</p>
                                    <p>（1）平均分配物品：老师把东西平均分给小朋友，每个小朋友 7 个正好分完，即东西的个数是 7 的倍数，可能只有 1～2 个选项能被 7 整除，剩二代一，假设选项都是 7 的倍数，则可以考虑其他方法解题。若能直接根据 7 的倍数选出答案，则可以秒杀。</p>
                                    <p>（2）平均数：每人分 7 个/平均分 7 个，总个数是 7 的倍数，平均的考法较狭窄。</p>
                                    <p>（3）三量关系，形如 A=B*C；总量=效率*时间，工程问题求总量，总量能被效率、时间整除；路程=速度*时间，S=V*T，假设走了 3 小时，路程能被 3 小时整除；总钱数=单价*个数，假设卖了 9 个，则总价是 9 的倍数，选项 80 不是 9 的倍数，则排除。</p>
                                </div>
                                <div class="border-radius">
                                    <p>【知识点】整除判定：不适用于做题，只是辅助计算，假设答案是 18 的倍
                                        数，无需用所有选项除以 18，可以采用口诀、因式分解、拆分。</p>
                                    <p>1.口诀：优先考虑。</p>
                                    <p>（1）3/9 看各位数字之和，这个点小学奥数学习过，54321 能否被 3 整除，
                                        54321 各位数字之和为 15，15 是 3 的倍数，则 54321 能被 3 整除，15 不是 9 的
                                        倍数，则 54321 不能被 9 整除。</p>
                                    <p>（2）5 看末位，一个数能否被 5 整除，千、百、十都是 5 的倍数，故只看
                                        最后一位能否被 5 整除，即看末位是否为 5 或 0。</p>
                                    <p>（3）4 看末两位，四位数的前两位为百位和千位，100/4，1000/4 均能整除，
                                        故无需看千位和百位，只看十位和个位，即末两位，如 1986 年、2014 年能否被
                                        4 整除，86/4、14/4 均有余数，故 1986 年、2014 年不是 4 的倍数，奥运会的年份均是 4 的倍数（前后 50 年）。</p>
                                    <p>（4）11、13、7 属于 10 年考查一回的数字，不在方法精讲课中讲解，方法
                                        精讲课中讲解高频考点。</p>
                                    <p>2.行测考试中 80%的内容都是小学数学，比高中数学简单。</p>
                                    <p>3.因式分解：数字不是高频考点。</p>
                                    <p>（1）18 是 9 的 2 倍，18=9*2，一个数既能被 9 整除，又能被 2 整除，则该
                                        数能被 18 整除，如 800、1000、1100 都不是 9 的倍数。因式分解后两个数需要
                                        互质，即没有公约数，9、2 除了 1，没有其他公约数，所以 9、2 互质。</p>
                                    <p>（2）假设 18=3*6，一个数能被 3 和 6 整除，但是不一定能被 18 整除，如
                                        12 既能被 3 整除，又能被 6 整除，不能被 18 整除，因为 3、6 有公约数 3，能被
                                        6 整除则一定能被 3 整除。</p>
                                    <p>（3）12=3*4，能被 3 和 4 整除，不能分成 2 和 6，因为 6 已经是 2 的倍数。</p>
                                    <p>（4）45=5*9，不能分解成 3*15，15 已经是 3 的倍数。</p>
                                    <p>（5）因式分解一般针对较大的数字，16 没有分解方法，不能分解成 4*4 或
                                        2*8，直接看即可，近 5 年没有考查 16 的倍数，考查倍数出题人会设计好数字。</p>
                                    <p>4.拆分法：7、13 本身无法分解，即质数。</p>
                                    <p>（1）判断 X 能否被 7 整除，X/7=7 的倍数+零头，看零头能否被 7 整除。如
                                        国考考查等差数列的和，共 7 项，则和为 7 的倍数，选项分别为 A.602、B.623、
                                        C.628、D.631，每个选项除以 7 需要除好几次，A 项 602=630-28，B 项 623=630-7，
                                        C 项 628=630-2，D 项 631=630+1，只有 A 项 602、B 项 623 是 7 的倍数。</p>
                                    <p>（2）找 13 的倍数，排除不是 13 倍数的选项。选项分别为 A.495、B.518、
                                        C.546、D.558，无需每个列算式除以 13，520 是 13 的倍数，选项分别为 A 项=520-25、
                                        B 项=520-2、C 项=520+26、D 项=520+28，只有 26 能被 13 整除，C 项当选。</p>
                                    <p>（3）7、13 等数字估算方法有口诀，但是复杂，不常用容易忘记，考虑拆
                                        分即可。</p>
                                </div>
