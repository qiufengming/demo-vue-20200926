                    <h3 class="text-c section-t" @click="sectionOpenClose('s3')">第三节  方程法</h3>
                    <div class="section-c" v-show="sectionOpen.s3">
                        <div class="border-radius">
                            <p>【知识点】普通方程→未知数个数=方程个数，方程可解，是定方程；未知
                                数个数＞方程个数（最典型的是两个未知数、一个方程的情况）→不定方程，利
                                用数字特性快速排除。设未知数技巧：</p>
                            <p>1.设小不设大（减少分数计算）。如：甲=3 乙，设乙为 x，甲为 3x。甲比乙多 5 个，设乙为 x，甲为 x+5，不建议设成减法形式（甲为 x，乙为 x-5），尽量
                                避免分数计算、减法计算</p>
                            <p>2.设中间量（方便列式）。题目中量比较多，设 3 或 4 个未知数，需要列出
                                3 或 4 个方程，甲乙有关，乙丙有关，乙丁有关，设乙为 x，据此表示出其他量。</p>
                            <p>3.问谁设谁（避免陷阱）：不能用前面两种时使用。</p>
                            <p>4.例如：甲：乙=3：2，为避免小数、分数，设甲为 3x，乙为 2x。</p>
                        </div>
                        <div class="border-radius">
                            <p>【知识点】不定方程：ax+by=M。</p>
                            <p>1.方法：分析奇偶、倍数、尾数等数字特性，结合选项排除。</p>
                            <p>2.奇偶：</p>
                            <p>（1）ax+by=M，两个未知数，一个方程，是不定方程，当系数 a、b 恰好一奇一偶时，考虑奇偶特性。偶数倍 x→一定是偶数，奇数倍 x→奇偶不定。</p>
                            <p>（2）引例：3x+4y=25，x=？（x、y 均为正整数）</p>
                            <p>A.2 &nbsp;&nbsp; B.3 &nbsp;&nbsp; C.4 &nbsp;&nbsp; D.5</p>
                            <p>答：系数 3、4 一奇一偶，考虑奇偶特性，25 是奇数，4y 是偶数，奇数+偶
                                数=奇数，3x 是奇数，则 x 只能为奇数，排除 A、C 项；剩二代一，代入 B 项：x
                                =3，解得：y=4，x、y 都是正整数，满足所有条件，B 项当选。</p>
                            <p>3.倍数：</p>
                            <p>（1）ax+by=M，当 a 或 b 与 M 有公因子时（公因子=公约数），考虑倍数特性。</p>
                            <p>（2）例：7x+3y=60，x+y 最大为多少？（x、y 均为正整数）</p>
                            <p>A.12 &nbsp;&nbsp; B.13 &nbsp;&nbsp; C.16 &nbsp;&nbsp; D.18</p>
                            <p>答：7 是质数，60、3 有公因子 3，则 7x 也有公因子 3，移项：7x=60-3y=3
                                *（20-y）=3*整数，7x 一定是 3 的倍数，则 x 是 3 的倍数，包括 3、6、9……，
                                x=3 时，y=13，问最大，此时加和为 16，排除比它小的 A、B 项；x=6 时，y=6，
                                加和＜C 项；x=9 时，y 为负，加和更小，C 项最大。</p>
                            <p>4.尾数：</p>
                            <p>（1）ax+by=M，当 a 或 b 尾数是 0 或 5 时，考虑尾数特性。</p>
                            <p>（2）例：37x+20y=271，x=？（x、y 均为正整数）</p>
                            <p>A.1 &nbsp;&nbsp; B.3 &nbsp;&nbsp; C.2 &nbsp;&nbsp; D.4</p>
                            <p>答：20y 的尾数为 0，271 的尾数为 1，则 37x 的尾数为 1，3*7=21，可知 x
                                的尾数为 3，对应 B 项。尾数可能有多种情况，不一定可以得到唯一答案，如：
                                4x=×2，x 的尾数可以是 3 或 8。</p>
                        </div>
                        <div class="border-radius">
                            <p>【知识点】不定方程组：通常是 3 个未知数、2 个方程。</p>
                            <p>1.第一类：未知数一定是整数。`a_1X＋b_1Y＋c_1Z＝M`，`a_2X＋b_2Y＋c_2Z＝N`。</p>
                            <p>2.方法：先消元转化为不定方程，再按不定方程求解。</p>
                            <p>3.例：`3X＋b_1Y＋c_1Z＝M①`，`6X＋b_2Y＋c_2Z＝N②`。②-①*2 消去 X，之后按照不定方程求解。</p>
                        </div>
                        <div class="border-radius">
                            <p>【知识点】不定方程组：</p>
                            <p>1.第二类：未知数不一定是整数，比如：时间、钱数可能取小数。</p>
                            <p>2.方法：</p>
                            <p>（1）特值法（一般赋零）：对于未知数不一定是整数的不定方程组，可以赋
                                其中 1 个未知数为零，进而快速计算出其他未知数。</p>
                            <p>（2）配系数。中学做法，①*？+②*？→所求，无需掌握。</p>
                        </div>
                        <div class="border-radius">
                            <p>【知识点】不定方程组：</p>
                            <p>1.第一类：未知数一定是整数。</p>
                            <p>2.方法：先消元转化为不定方程，再按不定方程求解。</p>
                            <p>3.第二类：未知数不一定是整数（时间、钱数）。</p>
                            <p>4.特值法（一般赋零）：</p>
                            <p>（1）对于未知数不一定是整数的不定方程组，可以赋其中 1 个未知数为零，
                                进而快速计算出其他未知数。</p>
                            <p>（2）配系数：中学做法，无需掌握</p>
