                    <h3 class="text-c section-t" @click="sectionOpenClose('s5')">第五节 行程问题</h3>
                    <div class="section-c" v-show="sectionOpen.s5">
                        <div class="border-radius">
                            <p>【知识点】行程问题：</p>
                            <p>1.三量关系：路程=速度*时间。</p>
                            <p>2.考查题型：</p>
                            <p>（1）基础行程：基本公式考查：路程=速度*时间（S=vt）。</p>
                            <p>（2）相对行程，非常重要，比如相遇、追及、船在水中运动，多次相遇追
                                及（两个主体存在相对运动）。</p>
                            <p>（3）比例行程，从正比、反比的角度考虑。</p>
                        </div>
                        <div class="border-radius">
                            <p>【知识点】隧道过桥：火车经过隧道/桥，火车不能看作一个点过桥。考查方式：</p>
                            <p>1.完全过桥：从车头上桥开始，到车尾下桥结束。分析一个不能看作点的物
                                体，可以从同一个部位分析，比如看车头到车头（或者看车尾到车尾），运动路
                                程包括桥长和车自身的长度。公式：`桥长+车长=v_车*t_(过桥)`。</p>
                            <p>2.整列火车都在桥上：从整个火车上桥，到车头要离开桥时结束。整个路程
                                比桥长少了一个车身长度，即运动路径为桥长-车长。公式：`桥长-车长=车*t_(在桥)`</p>
                        </div>
                        <div class="border-radius">
                            <p>【知识点】基础行程：</p>
                            <p>1.基本公式考查：路程=速度*时间</p>
                            <p>2.平均速度=总路程/总时间。</p>
                            <p>（1）比如 A→B，去的速度是 100，回来速度是 60，则来回的平均速度不是
                                （100+60）/2=80。考试遇到，可以把 80 当作是一个错误答案排除。</p>
                            <p>（2）比如来会的路程都是 300，去的时间是 3 分钟，回来的时间是 5 分钟，
                                整个 8 分钟的运动中，只有 3 分钟（不到一半的时间）是 100 的速度，超过一半
                                的时间是 60 为速度，只有来和回都是 4 分钟的时候，才能用相加除以 2 计算平
                                均值，因此答案更靠近 60。</p>
                            <p>（3）平均速度=（300+300）/（3+5）=75 米/分钟。</p>
                            <p>3.等距离平均速度公式：`\overline(v)=2v_1v_2/(v_1+v_2)`。</p>
                            <p>（1）`\bar(v)=2v_1v_2/(v_1+v_2)`，可以记作：2 积/和。比如上述例子，可以直接用
                                2*60*100/（60+100）=75。推导：`（S_去+S_回）÷（S/v_1+S/v_2）`，化简可得`\bar(v)=2v_1v_2/(v_1+v_2)`。</p>
                            <p>（2）注意：只有 S 相同才能用，如果去和回的路程不同则不能用，比如去
                                是走直线去，回是走曲线回，则不能套用此公式。</p>
                            <p>（3）常适用于：直线往返、上下坡往返。上下坡往返，是指 A→B，有上坡、有下坡，需要“翻山越岭”，这种情况下的往返，可以直接用公式。如果是去上
                                坡，回来变成下坡，依旧是直线距离，则属于直线往返。</p>
                            <p>①去的速度为 `v_上`，返回速度为 `v_下`，每一段都是来回各走一遍，每一段可以
                                用平均速度的公式计算。比如每段路程的平均速度都是 75，则全程的平均速度
                                就是 75。</p>
                            <p>②不论上下坡几次，都可以套用此公式。每段的坡距离不一定相同。</p>
                        </div>
                        <div class="border-radius">
                            <p>【知识点】相对行程：</p>
                            <p>1.直线相遇：两人方向相对，同时相向而行。</p>
                            <p>2.公式：`S_和=v_和*t_遇`。`S_和`：就是两人走的路程之和。</p>
                            <p>3.两人在直线两点出发，左边速度为 v1，右边速度为 `v_2`，经过相同的时间 t
                                到达相遇点，可以得到 `v_1t+v_2t=S_和`→`(v_1+v_2)t=S_和`→`v_和 t_遇=S_和`。</p>
                            <p>4.注意：这种题型考虑的是路程，不是物理概念中的“位移”</p>
                        </div>
                        <div class="border-radius">
                            <p>【知识点】直线追及：同时同向而行。</p>
                            <p>1.公式：`S_差=v_差*t_追`。`S_差`：追及刚开始时两人相差的距离。</p>
                            <p>2.同一个方向，才能是追及。开始起点，两人要有一个距离差，如果在一起
                                出发，就不能说“追及”。因此开始要有距离（虚线表示）。后面的人速度一定比
                                前面的人速度大，才能追上，因此前面人速度为 `v_小`，后面人速度为 `v_大`，蓝色路
                                程为 `v_小 t_追`，红色路程为 `v_大 t_追`，红色路程比蓝色路程多出部分就是开始两人的
                                路程差，故 `S_差=v_大 t-v_小 t`→`S_差=v_差*t_追`。</p>
                            <p>3.考试如果计算追及时间，只要找两人开始的路程差和两人的速度差。</p>
                        </div>
                        <div class="border-radius">
                            <p>【注意】</p>
                            <p>1.赋值多少不影响计算，赋值其他也可以做。</p>
                            <p>2.第一个追及过程，`S_间`是 12 的倍数，第二个追及过程，`S_间`是 18 的倍数，
                                路程既是 12 的倍数，又是 18 的倍数，故本题赋值间距为 36</p>
                            <p>3.本题有的同学担心，跑了 12 分钟之后，三人的位置关系会不会改变，如果有这种想法，题目 10 分钟可能做不完。行程问题，最关键的是“回到起点”
                                考虑。因此行程问题的思维，是考虑“追及刚开始时两人相差的距离”。</p>
                            <p>4.出现“再过多少分钟”的描述，很大概率会有陷阱，可以利用陷阱以坑治
                                坑。许多同学会忘记减掉 18 分钟，因此可以观察选项是否有差 18 分钟的，D 项
                                -18=C 项，则 D 项是从开始到追及的时间，C 项是再过的时间，故 C 项为所求。
                                考试能做要以做题为主，不能做再蒙。</p>
                        </div>
                        <div class="border-radius">
                            <p>【知识点】环形问题：大部分都是同点出发，不同点是少数。</p>
                            <p>1.环形相遇（同点反向出发）。</p>
                            <p>（1）公式：`S_和=v_和*t_遇`。</p>
                            <p>（2）相遇 1 次，`S_和=1圈`；相遇 N 次，`S_和=N 圈`。</p>
                            <p>&nbsp;&nbsp;- ①比如第一次相遇，左边走 `v_1t`，右边走 `v_2t`，合起来是全程，即 `v_1t+v_2t=1圈`→`v_和 t=1 圈`</p>
                            <p>&nbsp;&nbsp;- ②如果是相遇 n 次，路程和为 n 圈</p>
                            <p>（3）本质：每一次相遇到下一次相遇期间，两人走的路程和是一圈。第一
                                次相遇，记作①；之后两人继续走，第二次相遇，记作②；第一次相遇到第二次
                                相遇过程中，两人的路程又构成一个封闭的圆。因此每次相遇，到下一次相遇，
                                两人的路程和都是 1 圈。</p>
                            <p>（4）数学中的环形，不一定是圆形，只要是首尾相连（圆形、椭圆形、长
                                方形等），就是环形，封闭图形就是环形。</p>
                            <p>2.环形追及（同点同向出发）。</p>
                            <p>（1）公式：`S_差=v_差*t_追`。</p>
                            <p>（2）追上 1 次，`S_差=1 圈`；追上 N 次，`S_差=N 圈`。</p>
                            <p>&nbsp;&nbsp;- ①比如中学的体育课，A 在前面跑，B 要从背后追上 A，是因为 B 先跑完一
                                圈，B 后面跑的路程和 A 跑的路程相等，追上一次，路程差为 1 圈。比如第一个
                                人跑 200 米，第二个人要跑 1 圈+200 米才能追上。</p>
                            <p>&nbsp;&nbsp;- ②追上 n 次，路程差为 n 圈。本质：每一次追上到下一次追上期间，两人走
                                的路程差是一圈。比如第 n 次追上到第 n+1 次追上，快的人跑一整圈+△表示的
                                距离，慢的人只跑了△的距离。</p>
                            <p>（3）注意：公式中，追及是 `S_差=v_差 t`，相遇是` S_和=v_和 t`。`S_和`、`S_差`都是 1 圈。</p>
