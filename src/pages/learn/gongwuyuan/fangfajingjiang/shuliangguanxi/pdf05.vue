                        <div class="border-radius">
                            <p>【知识点】三集合：为考试中最主流的考法，公式有三种，公式越多越好做
                                题。三集合公式很长，考试一般考查纯代入，考查难度偏低。</p>
                            <p>1.三集合标准型公式：A+B+C-A∩B-A∩C-B∩C+A∩B∩C=总-都不</p>
                            <p>2.推导：三集合分别为 A、B、C，三者之间两两都有交叉关系，考试中从来
                                没考查过四集合和五集合。A+B+C，可以快速覆盖所有点，发现 A∩B 被 A 和 B
                                各计算了一遍，需要去掉重复的一遍，同理 A∩C 和 B∩C 都被计算了两遍，此时
                                都需要减去多余的一遍，为 A+B+C-A∩B-A∩C-B∩C；三集合最大的特殊点就是
                                中间的蓝色网状部分，为 A∩B∩C，A、B、C 的每个部分都含有 A∩B∩C，A∩B、
                                A∩C、B∩C 中都含有 A∩B∩C，加了三遍又减了三遍，此时需要补上一次，称之
                                为补漏，过程为“各加、去重、补漏”，A+B+C-A∩B-A∩C-B∩C+A∩B∩C=总-都
                                不，此时每个点都包含在公式中</p>
