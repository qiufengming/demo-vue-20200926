                        <div class="modal-wrap">
                            <h4 class="modal-t">二、余数型</h4>
                            <div class="modal-c">
                                <div class="border-radius">
                                    <p>【知识点】余数型：不是直接整除。</p>
                                    <p>1.一堆苹果分给每人 10 个，剩余 3 个。</p>
                                    <p>2.一堆苹果分给每人 10 个，还缺 3 个。</p>
                                    <p>3.多退少补，多余 3 个即退 3 个，此时正好每人 10 个，总数退 3 是 10 的
                                        倍数。缺 3 个即补 3 个，此时正好每人 10 个，总数补 3 是 10 的倍数。如 83-3
                                        是 10 的倍数，77+3 是 10 的倍数。这种方法能够节省找未知数列方程解方程
                                        的时间，快速解决选择题。</p>
                                </div>
                                <div class="border-radius">
                                    <p>【注意】</p>
                                    <p>1.等差数列求和：（首项+尾项）/2*项数。</p>
                                    <p>2.该类题目最早一次在 2007 年国考中出现过，特点是所有的数的和为定值，
                                        定值分为两部分，一部分是要求的，另一部分内部有比例关系，通过设未知数将
                                        比例关系表示出来，要求的数设为 y，则可得 ax+y=和，所以和-y=ax=a 的倍数。</p>
                                    <p>3.例：假如有 10 个人体重之和为 750kg，其中分成 3 个组，第一组总体重
                                        是第二组总体重的 2 倍，求第三组总体重可能是多少？A.226、B.232、C.237、
                                        D.244。</p>
                                    <p>答：正确答案是选 3 的倍数。总和分为多个部分，一个部分是要求的（第三
                                        组总体重），另一部分是给出了比例关系，即第一组的总体重是第二组总体重的
                                        2 倍，假设第二组体重为 x，第三组体重为 y，则第一组体重为 2x，列式：750=3x+y，
                                        750-y=3x 即 3 的倍数，750 也是 3 的倍数，所以 y 是 3 的倍数，只有 C 项符合。</p>
                                </div>
                            </div>
                        </div>
                        <div class="modal-wrap">
                            <h4 class="modal-t">三、比例型</h4>
                            <div class="modal-c">
                                <div class="border-radius">
                                    <p>【知识点】比例型：出题老师自由发挥的空间更大，既可以从分子上想，又可以从分母上想，还可以从和差上想。</p>
                                    <p>1.已知某班：男/女=3/5，问：</p>
                                    <p>（1）男生人数是 3 的倍数。</p>
                                    <p>（2）女生人数是 5 的倍数。</p>
                                    <p>（3）全班人数是 8 的倍数。</p>
                                    <p>（4）男女生人数差是 2 的倍数。</p>
                                    <p>（5）假设分数约掉了 x，即男/女=3x/5x，男生人数是 3x 即 3 的倍数，女
                                        生人数是 5x 即 5 的倍数，全班人数是 3x+5x=8x 即 8 的倍数，男女生人数差是
                                        5x-3x=2x 即 2 的倍数。</p>
                                    <p>2.A/B=m/n（m、n 互质，没有公约数）。m/n 是最简整数比，如男/女
                                        =3/5=30000/50000，但真实人数不会有几万人，不能说男生是 30000 的倍数，女
                                        生是 50000 的倍数，所以要化成最简整数比分析。如果不是最简整数比，做出来
                                        的答案就有问题了。</p>
                                    <p>（1）A 是 m 的倍数。</p>
                                    <p>（2）B 是 n 的倍数。</p>
                                    <p>（3）A+B 是 m+n 的倍数。</p>
                                    <p>（4）A-B 是 m-n 的倍数。</p>
                                </div>
                                <div class="border-radius">
                                    <p>【知识点】比例的常见形式：</p>
                                    <p>1.男生是女生的 3/5（分数）。</p>
                                    <p>2.男生与女生之比为 3：5（比例）=3/5。</p>
                                    <p>3.男生是女生的 60%（百分数）=60/100=3/5。</p>
                                    <p>4.男生是女生的 0.6 倍（倍数）=0.6/1=6/10=3/5。</p>
                                    <p>5.看到比例、百分数、倍数时可用比例的方法。</p>
                                    <p>6.公倍数的算法：5、6、8 没有公共的约数，提出 6、8 的公因子 2，剩余 5、3、4，此时完全互质，全部相乘：2*5*3*4=120。</p>
                                </div>
                            </div>
